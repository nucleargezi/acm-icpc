using mint = modint<mod99>;
NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<int> x(n), s(n - 1);
    std::cin >> x >> s, s.emplace_back(0);
    static mint dp[0721 << 3][0721 << 3], dpp[0721 << 3][0721 << 3];
    dpp[0][0] = 1;
    for (int i = 0; i < n; ++i) {
        for (int k = 0; k < s[i] + 1; ++k) {
            if (k) {
                dp[i + 1][k] = dp[i][k - 1] - dpp[i][k - 1] * x[i];
                dpp[i + 1][k] = dpp[i][k - 1];
            }
            dp[i + 1][k] +=
                dp[i][k + 1] * (k + 1) + dpp[i][k + 1] * x[i] * (k + 1);
            dpp[i + 1][k] += dpp[i][k + 1] * (k + 1);
        }
    }
    std::cout << dp[n][0] << '\n';
}

