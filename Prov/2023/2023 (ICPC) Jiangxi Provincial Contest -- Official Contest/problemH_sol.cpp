NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<int> a(n);
    std::cin >> a;
    vector<int> v;
    for (int i = 0, k; i < n; i = k) {
        k = i + 1;
        while (k < n and a[k] < a[i]) ++k;
        v.emplace_back(k - i);
    }
    sort(v);
    static bitset<500000> bit;
    bit.reset();
    bit[0] = 1;
    for (int i = 0, k; i < v.size(); i = k) {
        k = i;
        while (k < v.size() and v[k] == v[i]) ++k;
        int d = k - i, x = v[i];
        for (int k = 0; d; ++k) {
            int m = std::min(d, 1 << k);
            bit |= bit << (m * x);
            d -= m;
        }
    }
    Yes(bit[n >> 1]);
}

