NAME MeIoN_is_UMP45() {
    int n, m;
    std::cin >> n >> m;
    vector<ll> v(n);
    for (ll i = 1, x, y, w; i < n; ++i) {
        std::cin >> x >> y >> w;
        --x, --y;
        v[x] ^= w, v[y] ^= w;
    }
    for (ll i = 0, op, x, y, w; i < m; ++i) {
        std::cin >> op;
        if (op == 1) {
            std::cin >> x >> y >> w;
            --x, --y;
            v[x] ^= w, v[y] ^= w;
        } else {
            std::cin >> x;
            --x;
            std::cout << v[x] << '\n';
        }
    }
}

