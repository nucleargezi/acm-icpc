NAME MeIoN_is_UMP45() {
    vector<int> v[9];
    v[1].emplace_back(0), v[2].emplace_back(1), v[3].emplace_back(2);
    for (int i = 0, a, b; i < 12; ++i) {
        std::cin >> a >> b, --a;
        for (int i = 0, ok = 1; i < 9 and ok; ++i) {
            for (int k = 0; k < v[i].size(); ++k) {
                if (v[i][k] == a) {
                    ok = 0;
                    v[i + b].insert(v[i + b].end(), v[i].begin() + k, v[i].end());
                    v[i].erase(v[i].begin() + k, v[i].end());
                    break;
                }
            }
        }
    }
    std::cout << (v[8].size() == 3 ? 'Y' : 'N') << '\n';
}

