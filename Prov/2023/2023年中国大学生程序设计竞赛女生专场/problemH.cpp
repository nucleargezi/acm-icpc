using mint = modint<mod17>;
struct MeIoN_ACAM {
    static constexpr int ALPHABET = 26;
    struct Node {
        int len, fail;
        std::array<int, ALPHABET> next;
        Node() : len { 0 } , fail { 0 } , next {} {}
    };
    std::vector<Node> t;
    vector<mint> cnt, s;
    MeIoN_ACAM() {
        init();
    }
    void init() {
        t.assign(2, Node());
        t[0].next.fill(1);
        t[0].len = -1;
        cnt.assign(2, {});
        s.assign(2, {});
    }
    int newNode() {
        t.emplace_back();
        return t.size() - 1;
    }
    int add(const std::string& a) {
        int p = 1;
        for (auto c : a) {
            int x = c - 'a';
            if (t[p].next[x] == 0) {
                t[p].next[x] = newNode();
                cnt.emplace_back(0);
                s.emplace_back(0);
                t[t[p].next[x]].len = t[p].len + 1;
            }
            p = t[p].next[x];
        }
        cnt[p] += 1;
        s[p] += a.length();
        return p;
    }
    void work() {
        std::queue<int> q;
        q.push(1);
        while (!q.empty()) {
            int x = q.front();
            q.pop();
            s[x] += s[fail(x)];
            cnt[x] += cnt[fail(x)];
            for (int i = 0; i < ALPHABET; i++) {
                if (t[x].next[i] == 0) {
                    t[x].next[i] = t[t[x].fail].next[i];
                } else {
                    t[t[x].next[i]].fail = t[t[x].fail].next[i];
                    q.push(t[x].next[i]);
                }
            }
        }
    }
    mint quis(const string &a) {
        int p = 1;
        mint ret, d, c;
        for (const char ch : a) {
            p = next(p, ch - 'a');
            c += 1;
            d += (c + 1) * cnt[p] - s[p];
            ret += d;
        }
        iroha ret;
    }
    int next(int p, int x) { return t[p].next[x]; }
    int fail(int p)        { return t[p].fail; }
    int len(int p)         { return t[p].len; }
    int size()             { return t.size(); }
};
using AC = MeIoN_ACAM;
NAME MeIoN_is_UMP45() {
    int n, m;
    std::cin >> n >> m;
    AC ac;
    for (int i = 0; i < n; ++i) {
        static string s;
        std::cin >> s;
        ac.add(s);
    }
    ac.work();
    while (m--) {
        static string q;
        std::cin >> q;
        std::cout << ac.quis(q) << '\n';
    }
}

