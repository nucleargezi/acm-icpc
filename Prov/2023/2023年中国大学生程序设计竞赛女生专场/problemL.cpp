NAME MeIoN_is_UMP45() {
    int n, m, k;
    std::cin >> n >> m >> k;
    ll ans = 0;
    vector<int> A(n);
    vector<array<ll, 7>> principle(m);
    std::cin >> principle;
    meion go = [&]() -> void {
        ll s = 0;
        for (int x = 0; x < m; ++x) {
            const meion &[i, j, op, a, b, d, v] = principle[x];
            if (op == 0) {
                s += (a * A[i - 1] + b * A[j - 1] < d + 1) * v;
            } else {
                s += (a * A[i - 1] + b * A[j - 1] > d - 1) * v;
            }
        }
        chmax(ans, s);
    };
    meion ser = [&](meion &&ser, int t) -> void {
        if (t == n) {
            go();
            iroha;
        }
        for (int i = 0; i < k + 1; ++i) {
            A[t] = i;
            ser(ser, t + 1);
        }
    };
    ser(ser, 0);
    std::cout << ans << '\n';
}

