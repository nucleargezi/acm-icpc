NAME MeIoN_is_UMP45() {
    int g;
    std::cin >> g;
    hash_map<ll> ma;
    for (int i = 0, x, y; i < g; ++i) {
        std::cin >> x >> y;
        ma[x] += y;
    }
    int n;
    std::cin >> n;
    vector<int> in(n);
    vector<vector<pair<int, int>>> v(n);
    hash_map<vector<pair<int, int>>> mv;
    int ans = 0;
    MeIoN_Que<int> q;
    for (int i = 0, sz; i < n; ++i) {
        std::cin >> sz;
        for (int k = 0, x, y; k < sz; ++k) {
            std::cin >> x >> y;
            if (ma[x] < y) {
                mv[x].emplace_back(y, i);
                ++in[i];
            }
        }
        std::cin >> sz;
        for (int k = 0, x, y; k < sz; ++k) {
            std::cin >> x >> y;
            v[i].emplace_back(x, y);
        }
    }
    for (int i = 0; i < n; ++i) {
        if (in[i] == 0) {
            q.emplace_back(i);
        }
    }
    mv.enumerate_all([&](meion &a, meion &b) -> void { sort(b, greater()); });
    while (not q.empty()) {
        int n = q.front();
        q.pop();
        ++ans;
        for (meion &[x, y] : v[n]) {
            ll &t = ma[x];
            t += y;
            while (not mv[x].empty() and mv[x].back().first < t + 1) {
                const int &i = mv[x].back().second;
                if (not --in[i]) {
                    q.emplace_back(i);
                }
                mv[x].pop_back();
            }
        }
    }
    std::cout << ans << '\n';
}

