NAME MeIoN_is_UMP45() {
    int n, k;
    std::cin >> n >> k;
    vector<point<ll>> points(n);
    std::cin >> points;
    convex_polygon polygon(points);
    points.insert(points.end(), points.begin(), points.end());
    ll ans = 0;
    for (int b = 0, c = k, a = c; b < n; ++b, ++c) {
        while (a < c + 1) ++a; 
        while (ccw(points[c], points[a + 1], points[b]) == 1 and
            triangle_area_2(points[a], points[b], points[c]) <=
                triangle_area_2(points[a + 1], points[b], points[c])) {
            ++a;
        }
        chmax(ans, polygon.area_between(b, c) +
                       triangle_area_2(points[a], points[b], points[c]));
    }
    std::cout << 0.5L * ans << '\n';
}

