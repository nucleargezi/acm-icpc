NAME MeIoN_is_UMP45() {
    int n;
    ll k;
    std::cin >> n >> k;
    vector<ll> a(n);
    std::cin >> a;
    vector<ll> b(n - 1);
    for (int i = 0; i < n - 1; ++i) {
        b[i] = std::abs(a[i] - a[i + 1]);
    }
    Seg<monoid_gcd<ll>> seg(a), segg(b);
    ll ans = seg.prod_all();
    ll gcd = a[0];
    vector<int> pla{0};
    for (int i = 1; i < n; ++i) {
        if (std::gcd(gcd, a[i]) != gcd) {
            pla.emplace_back(i);
            gcd = std::gcd(gcd, a[i]);
        }
    }
    for (int i = n; i--; ) {
        gcd = a[i] + k;
        if (i + 1 != n) {
            gcd = std::gcd(gcd, seg.prod(i + 1, n));
        }
        for (const int k : pla) {
            if (k > i) break;
            ll _gcd = k ? std::gcd(gcd, seg.prod(0, k)) : gcd;
            if (i > k) _gcd = std::gcd(_gcd, segg.prod(k, i));
            chmax(ans, _gcd);
        }
    }
    std::cout << ans << '\n';
}

