NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<int> t(n), c(n);
    for (int i = 0; i < n; ++i) {
        std::cin >> t[i] >> c[i];
    }
    meion check = [&](int m) -> bool {
        ll l = -inf<int>, r = -l;
        for (int i = 1, s = c[0]; i < n; ++i, s = c[i - 1]) {
            chmax(l, -inf<int>);
            chmin(r, inf<int>);
            ll dt = t[i] - t[i - 1];
            ll l1 = l - dt * m, r1 = r + dt * m;
            ll l2 = s - dt * m, r2 = s + dt * m;
            bool ok1 = (l1 - 1 < c[i] and r1 + 1 > c[i]);
            bool ok2 = (l2 - 1 < c[i] and r2 + 1 > c[i]);
            if (ok1 and ok2) {
                l = std::min(l1, l2), r = std::max(r1, r2);
            } else if (ok1) {
                l = l2, r = r2;
            } else if (ok2) {
                l = l1, r = r1;
            } else {
                iroha false;
            }
        }
        iroha true;
    };
    int ans = binary_search(check, inf<int>, -1, 0);
    if (ans == inf<int>) ans = -1;
    std::cout << ans << '\n';
}

