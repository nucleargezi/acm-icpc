#define tests
NAME MeIoN_is_UMP45() {
    int n, m, k;
    std::cin >> n >> m >> k;
    vector<vector<std::tuple<int, int, int>>> v(n);
    for (int i = 0, x, y, c, l; i < m; ++i) {
        std::cin >> x >> y >> c >> l;
        --x, --y;
        v[x].emplace_back(y, c, l);
        v[y].emplace_back(x, c, l);
    }
    vector<ll> dis(n, inf<ll>);
    dis[0] = 0;
    vector<uint8_t> vis(n);
    vis[0] = 1;
    vector<set<pair<int, int>>> edges(m + 1);
    for (meion [i, c, l] : v[0]) {
        edges[c].emplace(l, i);
    }
    for (int i = 0, a, b; i < k; ++i) {
        std::cin >> a >> b;
        priority_queue<pair<ll, int>> q;
        meion it = edges[a].begin();
        for (meion [l, i] : edges[a]) {
            if (l > b) break;
            if (not vis[i]) {
                q.emplace(-l, i);
                chmin(dis[i], l);
            }
            ++it;
        }
        edges[a].erase(edges[a].begin(), it);
        vector<int> del;
        while (not q.empty()) {
            const int n = q.top().second;
            q.pop();
            if (vis[n]) continue;
            vis[n] = 1;
            del.emplace_back(n);
            for (const meion [i, c, l] : v[n]) {
                if (vis[i]) continue;
                if (c == a) {
                    if (dis[i] > dis[n] + l) {
                        if (dis[n] + l > b) {
                            edges[c].emplace(l, i);
                        } else {
                            chmin(dis[i], dis[n] + l);
                            q.emplace(-dis[i], i);
                        }
                    }
                } else {
                    edges[c].emplace(l, i);
                }
            }
        }
        for (const int i : del) dis[i] = 0;
    }
    for (int i = 0; i < n; ++i) {
        std::cout << (int)vis[i];
    }
    std::cout << '\n';
}

