NAME MeIoN_is_UMP45() {
    int n, k;
    std::cin >> n >> k;
    vector<ll> a(n);
    std::cin >> a;
    a.insert(a.begin(), 0);
    a.emplace_back(0);
    string vis;
    std::cin >> vis;
    vis = '1' + vis + '1';
    for (char &c : vis) {
        c -= '0';
    }

    vector<ll> dp(n + 2), ddp(n + 2);
    MeIoN_Que<int> q;
    q.reserve(n);
    q.emplace_back(0);
    for (int i = 1; i < n + 2; ++i) {
        while (not q.empty() and q.front() + k < i) {
            q.pop();
        }
        dp[i] = dp[q.front()] + a[i];
        if (vis[i]) {
            q.clear();
        }
        while (not q.empty() and dp[q.back()] >= dp[i]) {
            q.pop_back();
        }
        q.emplace_back(i);
    }
    
    q.clear();
    q.emplace_back(n + 1);
    for (int i = n; i > 0; --i) {
        while (q.front() - k > i) {
            q.pop();
        }
        ddp[i] = ddp[q.front()] + a[i];
        if (vis[i]) {
            q.clear();
        }
        while (not q.empty() and ddp[q.back()] > ddp[i] - 1) {
            q.pop_back();
        }
        q.emplace_back(i);
    }

    int quis;
    std::cin >> quis;
    vector c(a);
    meion sol = [&](int p) -> ll {
        ll ret = inf<ll>;
        static ll A[514514];
        MeIoN_Que<int> q;
        for (int i = std::max(0, p - k); i < p; ++i) {
            while (not q.empty() and q.front() + k < i) {
                q.pop();
            }
            if (vis[i]) {
                q.clear();
            }
            while (not q.empty() and dp[q.back()] > dp[i] - 1) {
                q.pop_back();
            }
            q.emplace_back(i);
            A[i] = dp[i];
        }
        for (int i = p; i < std::min(n + 2, p + k); ++i) {
            while (not q.empty() and q.front() + k < i) {
                q.pop();
            }
            A[i] = A[q.front()] + a[i];
            chmin(ret, A[i] + ddp[i] - c[i]);
            while (not q.empty() and A[q.back()] > A[i] - 1) {
                q.pop_back();
            }
            if (vis[i]) {
                q.clear();
            }
            q.emplace_back(i);
        }
        iroha ret;
    };
    for (int i = 0; i < quis; ++i) {
        static int p;
        static ll x;
        std::cin >> p >> x;
        std::swap(a[p], x);
        std::cout << sol(p) << '\n';
        std::swap(a[p], x);
    }
}
