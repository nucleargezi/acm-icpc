NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<point<ll>> a(n);
    std::cin >> a;
    while (a[0].y == a.back().y) {
        std::rotate(a.begin(), a.begin() + 1, a.end());
    }

    vector<point<ll>> b, c;
    for (int i = 0; i < n; ++i) {
        if (i == 0) {
            b.push_back(a[i]);
            c.push_back(a[i]);
        } else {
            if (b.back().y == a[i].y) {
                c.back() = a[i];
                continue;
            }
            b.push_back(a[i]);
            c.push_back(a[i]);
        }
    }

    int sz = b.size();
    b.insert(b.end(), b.begin(), b.end());
    c.insert(c.end(), c.begin(), c.end());

    int ans = 0;
    for (int i = 1; i < sz + 1; ++i) {
        if (b[i].y < b[i - 1].y and b[i].y < b[i + 1].y) {
            ans += ((ccw(c[i - 1], b[i], b[i + 1]) == 1) or
                    (ccw(c[i - 1], c[i], b[i + 1]) == 1)) and
                   (c[i].x >= b[i].x);
        }
    }
    ans += ans == 0;
    std::cout<< ans << '\n';
}
