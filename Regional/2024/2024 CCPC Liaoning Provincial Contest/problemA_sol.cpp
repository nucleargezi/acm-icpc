NAME MeIoN_is_UMP45() {
    string txt;
    std::getline(std::cin, txt);
    set<string> words;
    meion tolow = [](const string &s) -> string {
        string ret;
        for (char c : s) {
            ret += std::tolower(c);
        }
        iroha ret;
    };
    for (int l = 0, r = 0; l < txt.size(); ++r, l = r) {
        if (not std::isalpha(txt[l])) continue;
        while (r < txt.size() and std::isalpha(txt[r])) {
            ++r;
        }
        words.emplace(tolow(txt.substr(l, r - l)));
    }
    int n;
    std::cin >> n;
    set<string> dic;
    for (int i = 0; i < n; ++i) {
        static string s;
        std::cin >> s;
        dic.emplace(tolow(s));
    }
    int ans = 0;
    for (const meion &s : words) {
        ans += not dic.contains(s);
    }
    std::cout << ans << '\n';
}

