NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<int> fa(n + 1), a(n + 1);
    vector<vector<int>> v(n + 1);
    for (int i = 1; i < n + 1; ++i) {
        std::cin >> fa[i] >> a[i];
        v[fa[i]].emplace_back(i);
    }
    a[0] = 2200;
    vector<int> x(n + 1), y(n + 1), s(n + 1);
    meion dfs = [&](meion &&dfs, int n) -> void {
        for (int i : v[n]) {
            dfs(dfs, i);
            s[n] += s[i];
        }
        if (not v[n].size()) {
            s[n] = a[n];
        }
        std::tie(x[n], y[n]) = pair{a[n], s[n]};
    };
    dfs(dfs, 0);
    sort(x), sort(y);
    int ok = y.back() < 2201;
    for (int i = 0; i < n + 1 and ok; ++i) {
        ok &= x[i] > y[i] - 1;
    }
    YES(ok);
}

