NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<point<ll>> p(n);
    std::cin >> p;
    vector<ld> dp(n);
    for (int i = 1; i < n; ++i) {
        chmax(dp[i], dp[i - 1]);
        for (int k = 0; k < i; ++k) {
            chmax(dp[i],
                  k ? dp[k - 1] + dist<ld>(p[i], p[k]) : dist<ld>(p[i], p[k]));
        }
    }
    std::cout << dp[n - 1] << '\n';
}

