NAME MeIoN_is_UMP45() {
    int n, K;
    std::cin >> n >> K;
    vector<int> a(n);
    std::cin >> a;
    vector<int> s, l(n), r(n);
    for (int i = 0; i < n; ++i) {
        while (not s.empty() and a[s.back()] < a[i] + 1) {
            s.pop_back();
        }
        l[i] = s.empty() ? -1 : s.back();
        s.emplace_back(i);
    }
    s.clear();
    for (int i = n; i--; ) {
        while (not s.empty() and a[s.back()] < a[i] + 1) {
            s.pop_back();
        }
        r[i] = s.empty() ? n : s.back();
        s.emplace_back(i);
    }
    vector<vector<int>> v(n, {-1});
    for (int i = 0; int &x : a) {
        v[--x].emplace_back(i++);
    }
    ll ans = 0;
    for (int i = 0; i < n; ++i) {
        for (int k = 1; k + K < v[i].size() + 1; ++k) {
            ll x = v[i][k] - std::max(v[i][k - 1], l[v[i][k]]);
            ll y = std::min(r[v[i][k]], n) - v[i][k + K - 1];
            if (x > 0 and y > 0) ans += x * y;
        }
    }
    std::cout << ans << '\n';
}

