NAME MeIoN_is_UMP45() {
    ll n;
    std::cin >> n;
    vector<ll> a;
    for (int i = 1; i * i < n + 1; ++i) {
        if (n % i == 0) {
            a.emplace_back(i);
            a.emplace_back(n / i);
        }
    }
    unique(a);
    ll ans = 1;
    for (int i = 0; i + 1 < a.size(); ++i) {
        ans += n / a[i] * (a[i + 1] - a[i]);
    }
    std::cout << ans << '\n';
}

