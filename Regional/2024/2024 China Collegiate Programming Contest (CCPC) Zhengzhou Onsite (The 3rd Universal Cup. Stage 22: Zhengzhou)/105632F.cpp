NAME MeIoN_is_UMP45() {
    ll n, k, q;
    std::cin >> n >> k >> q;
    vector<ll> a(n), b(n);
    for (int i = 0; i < n; ++i) {
        std::cin >> a[i] >> b[i];
    }
    vector<ll> dp(n * 3);
    for (int i = 0; i < 3; ++i) {
        for (int x = 0; x < n; ++x) {
            dp[i * n + x] = i * k + a[x] - 1;
            chmax(dp[i * n + x], i + x ? dp[i * n + x - 1] : 0);
            dp[i * n + x] += b[x];
        }
    }
    ll s = std::max(qsum(b, 0ll), k);
    meion out = [&](ll x) -> void {
        ll t = (x - 1) / k + 1;
        std::cout << t << ' ' << x - (t - 1) * k << '\n';
    };
    for (int i = q, x, y; i--; ) {
        std::cin >> x >> y;
        --x, --y;
        out(x < 3 ? dp[x * n + y] : dp[n + n + y] + (x - 2) * s);
    }
}

