NAME MeIoN_is_UMP45() {
    array<pair<int, string>, 8> a; 
    for (meion &[v, name] : a) {
        std::cin >> name >> v;
    }
    for (int i = 0; i < 8; i += 2) {
        chmax(a[i], a[i + 1]);
    }
    chmax(a[0], a[2]), chmax(a[4], a[6]);
    if (a[0] < a[4]) std::swap(a[0], a[4]);
    std::cout << a[0].second << " beats " << a[4].second << '\n';
}

