NAME MeIoN_is_UMP45() {
    int n, m, q;
    std::cin >> n >> m >> q;
    vector<vector<pair<int, int>>> v(n);
    for (int i = 0, x, y; i < m; ++i) {
        std::cin >> x >> y;
        x = (x % n + n) % n;
        if (y) v[x].emplace_back(((x + y) % n + n) % n, y);
    }
 
    // tarjan
    vector<int> dfn(n), low(n), s, id(n), sz(n);
    vector<uint8_t> vis(n);
    vector<vector<int>> scc(n);
    int tot = 0, tarjan_cnt = 0;

    meion tarjan = [&](meion &&tarjan, int n) -> void {
        dfn[n] = low[n] = ++tot;
        s.emplace_back(n);
        vis[n] = 1;
        for (meion [i, w] : v[n]) {
            if (not dfn[i]) {
                tarjan(tarjan, i);
                chmin(low[n], low[i]);
            } else if (vis[i]) {
                chmin(low[n], dfn[i]);
            }
        }
        if (dfn[n] == low[n]) {
            while (true) {
                int i = s.back();
                s.pop_back();
                id[i] = tarjan_cnt;
                sz[tarjan_cnt]++;
                vis[i] = 0;
                scc[tarjan_cnt].emplace_back(i);
                if (i == n) break;
            }
            ++tarjan_cnt;
        }
    };
    for (int i = 0; i < n; ++i) {
        if (not dfn[i]) tarjan(tarjan, i);
    }
    sz.resize(tarjan_cnt);
    id.resize(tarjan_cnt);
    scc.resize(tarjan_cnt);
    vis.assign(tarjan_cnt, 0);
 
    vector<vector<pair<int, int>>> g(n);
    vector<ll> dis(n, inf<ll>);
    vector<int> cnt(n);
    vector<uint8_t> vis_sp(n);

    for (meion &s : scc) {
        for (int x : s) {
            for (meion [to, w] : v[x]) {
                if (id[to] == id[x]) {
                    g[x].emplace_back(to, w);
                    g[to].emplace_back(x, -w);
                } 
            }
        }
 
        // spfa
        bool flag = 1;
        MeIoN_Que<int> q;
        q.emplace_back(s[0]);
        dis[s[0]] = 0;
        while (not q.empty() and flag) {
            int n = q.front();
            q.pop();
            vis_sp[n] = 0;
            for (meion [to, w] : g[n]) {
                if (chmin(dis[to], dis[n] + w)) {
                    if (not vis_sp[to]) {
                        q.emplace_back(to);
                        vis_sp[to] = 1;
                    }
                    if (++cnt[to] > 1) {
                        flag = false;
                        break;
                    }
                }
            }
        }
        vis[id[s[0]]] = not flag;
    }
 
    vector<vector<int>> adj(tarjan_cnt);
    for (int i = 0; i < n; ++i) {
        for (meion [x, w] : v[i]) {
            if (id[i] != id[x]) {
                adj[id[x]].emplace_back(id[i]);
            }
        }
    }
    meion dfs = [&](meion &&dfs, int n) -> void {
        for (int i : adj[n]) {
            if (not vis[i]) {
                vis[i] = 1;
                dfs(dfs, i);
            }
        }
    };
    for (int i = 0; i < tarjan_cnt; ++i) {
        if (vis[i]) {
            dfs(dfs, i);
        }
    }

    for (int i = 0, x; i < q; ++i) {
        std::cin >> x;
        x = (x % n + n) % n;
        Yes(vis[id[x]]);
    }
}

