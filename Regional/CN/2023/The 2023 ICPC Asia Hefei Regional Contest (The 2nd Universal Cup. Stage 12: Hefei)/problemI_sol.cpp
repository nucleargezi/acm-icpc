    vector<string> s(n * n);
    map<char, array<int, 2>> mp;
    map<char, array<map<char, int>, 2>> inchar;
    for (meion &str : s) {
        std::cin >> str;
        if (str.size() == 1) {
            ++mp[str[0]][0];
        } else {
            ++mp[str[0]][1];
            ++mp[str[1]][0];
            ++inchar[str[1]][0][str[0]];
            ++inchar[str[0]][1][str[1]];
        }
    }
    map<int, array<int, 2>> mn;
    map<int, array<map<int, int>, 2>> inn;
    for (int i = 0; i < n; ++i) {
        for (int k = 0; k < n; ++k) {
            int x = i * k;
            if (x < n) {
                ++mn[x][0];
            } else {
                ++mn[x / n][1];
                ++mn[x % n][0];
                ++inn[x % n][0][x / n];
                ++inn[x / n][1][x % n];
            }
        }
    }
    map<array<int, 2>, string> mpmp;
    for (meion [x, y] : mp) {
        mpmp[y] += x;
    }
    map<array<int, 2>, vector<int>> mnmn;
    for (meion [x, y] : mn) {
        mnmn[y].emplace_back(x);
    }
    vector<pair<array<int, 2>, string>> a{mpmp.begin(), mpmp.end()};
    vector<pair<array<int, 2>, vector<int>>> b{mnmn.begin(), mnmn.end()};

    string ans(n, '1');
    
    for (int i = 0; i < a.size(); ++i) {
        const meion &[x, s] = a[i];
        const meion &[y, v] = b[i];
        for (int i : v) {
            int ok = 1;
            vector<int> a[2];
            for (int k = 2; k--; ) {
                for (meion [x, y] : inn[i][k]) {
                    a[k].emplace_back(y);
                }
                sort(a[k]);
            }
            for (char c : s) {
                vector<int> b[2];
                for (int k = 2; k--; ) {
                    for (meion [x, y] : inchar[c][k]) {
                        b[k].emplace_back(y);
                    }
                    sort(b[k]);
                }
                if (a[0] == b[0] and a[1] == b[1]) {
                    ans[i] = c;
                }
            }
        }
    }
