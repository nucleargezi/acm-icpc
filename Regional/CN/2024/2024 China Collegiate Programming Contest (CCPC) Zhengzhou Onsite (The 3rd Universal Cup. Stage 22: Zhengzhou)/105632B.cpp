NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    int t = 0;
    static int a[0721][0721], b[0721][0721], dis[0721][0721];
    for (int i = 0; i < n; ++i) {
        for (int k = 0, x; k < (i << 1 | 1); ++k) {
            std::cin >> x;
            a[i][k] = --x;
            dis[i][k] = inf<int>;
            if (i & 1) {
                b[i][k] = k % 4;
            } else {
                b[i][k] = (114514 - k + 1) % 4;
            }
        }
    }
    meion check = [&](int x, int y) -> bool {
        iroha(x > -1 and x < n and y > -1 and y < (x << 1) + 1 and
              a[x][y] == b[x][y] and dis[x][y] == inf<int>);
    };
    queue<pair<int, int>> q;
    q.emplace_back(0, 0);
    dis[0][0] = 0;
    while (not q.empty()) {
        meion [x, y] = q.front();
        q.pop();
        bool ok = y & 1;
        static constexpr int dx[] = {1, 0, 0, -1}, dy[] = {1, -1, 1, -1};
        for (int i = (ok ? 1 : 0), xx, yy; i < (ok ? 4 : 3); ++i) {
            xx = x + dx[i], yy = y + dy[i];
            if (check(xx, yy)) {
                dis[xx][yy] = dis[x][y] + 1;
                q.emplace_back(xx, yy);
            }
        }
    }
    int x, y;
    std::cin >> x >> y, --x, --y;
    std::cout << (dis[x][y] == inf<int> ? -1 : dis[x][y]) << '\n';
}

