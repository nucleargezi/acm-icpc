NAME MeIoN_is_UMP45() {
    int n, K;
    string s;
    std::cin >> n >> K >> s;
    string ss = s;
    sort(ss);

    array<int, 26> cs{}, css{};
    for (char c : s) {
        ++cs[c - 'a'];
        ++css[c - 'a'];
    }
    string ans;
    for (int i = 0; i < n; ++i) {
        --cs[s[i] - 'a'];
        for (int k = 0; k < 26; ++k) {
            if (not css[k]) continue;
            --css[k];
            int f = n - i - (k == s[i] - 'a');
            for (int j = 0; j < 26; ++j) {
                f -= std::min(cs[j], css[j]);
            }
            if (f < K + 1) {
                ans += 'a' + k;
                K -= (k != s[i] - 'a');
                break;
            } else {
                ++css[k];
            }
        }
    }
    std::cout << ans << '\n';
}

