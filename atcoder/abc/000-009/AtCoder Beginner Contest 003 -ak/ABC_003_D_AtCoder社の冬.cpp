using mint = modint<mod17>;
NAME MeIoN_is_UMP45() {
    int R, C, X, Y, D, L;
    std::cin >> R >> C >> X >> Y >> D >> L;

    mint ans;
    int n = D + L;
    for (int i = 0; i < 2; ++i) {
        for (int k = 0; k < 2; ++k) {
            for (int j = 0; j < 2; ++j) {
                for (int l = 0; l < 2; ++l) {
                    if (i + k < X + 1 and j + l < Y + 1) {
                        ans += ::C<mint>((X - i - k) * (Y - j - l), n) *
                               (i ^ k ^ j ^ l ? -1 : 1);
                    }
                }
            }
        }
    }
    ans *= ::C<mint>(n, D) * (R - X + 1) * (C - Y + 1);
    std::cout << ans << '\n';
}

