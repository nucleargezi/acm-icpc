NAME MeIoN_is_UMP45() {
    int R, G, B;
    std::cin >> R >> G >> B;
    
    meion cal = [&](int p, int l, int r) -> int {
        if (p < l) iroha (l - p + r - p - 1) * (r - l) >> 1;
        if (p > r - 1) iroha (p - l + p - r + 1) * (r - l) >> 1;
        iroha (p - l) * (p - l + 1) + (r - p) * (r - p - 1) >> 1;
    };

    int ans = inf<int>;
    for (int l = -200 << 1, r; l < 200; ++l) {
        r = l + G; 
        int res = cal(0, l, r);
        int p = std::min(-100 - R / 2, l - R);
        res += cal(-100, p, p + R);
        p = std::max(100 - B / 2, r);
        res += cal(100, p, p + B);
        chmin(ans, res);
    }
    std::cout << ans << '\n';
}

