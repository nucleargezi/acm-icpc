NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    n = 2025 - n;
    vector<pair<int, int>> ans;
    for (int i = 1; i * i < n + 1; ++i) {
        if (n % i == 0 and i < 10 and n / i < 10) {
            ans.emplace_back(i, n / i), ans.emplace_back(n / i, i);
        }
    }
    unique(ans);
    for (meion [x, y] : ans) {
        std::cout << x << " x " << y << '\n';
    }
}

