NAME MeIoN_is_UMP45() {
    ll n, h, a, b, c, d, e;
    std::cin >> n >> h >> a >> b >> c >> d >> e;
    ll ans = inf<ll>;
    for (int i = 0; i < n + 1; ++i) {
        ll x = ((n - i) * e - h - b * i) / (d + e) + 1;
        if ((n - i) * e - h - b * i < 0) x = 0;
        if (x + i > n) continue;
        ans = std::min(ans, i * a + x * c);
    }
    std::cout << ans << '\n';
}

