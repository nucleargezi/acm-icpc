NAME MeIoN_is_UMP45() {
    using P = point<ll>;
    P A, B;
    std::cin >> A >> B;
    int n;
    std::cin >> n;
    vector<P> a(n);
    std::cin >> a;
    a.emplace_back(a[0]);
    int ans = 2;
    for (int i = 0; i < n; ++i) {
        ans += count_cross(segment(a[i], a[i + 1]), segment(A, B), false) == 1;
    }
    std::cout << (ans >> 1) << '\n';
}

