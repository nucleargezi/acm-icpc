NAME MeIoN_is_UMP45() {
    string s;
    std::cin >> s;
    while (not s.empty()) {
        if (s.back() == 'h' and s.size() > 1 and s.end()[-2] == 'c') s.pop_back(), s.pop_back();
        else if (s.back() == 'o') s.pop_back();
        else if (s.back() == 'k') s.pop_back();
        else if (s.back() == 'u') s.pop_back();
        else break;
    }
    YES(s.empty());
}

