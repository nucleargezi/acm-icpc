NAME MeIoN_is_UMP45() {
    short n, m, K;
    std::cin >> n >> m >> K;
    static constexpr short N = 0721 | 0721 >> 1;
    static short a[N][N];
    vector<pair<short, short>> p;
    p.reserve(n * m);
    
    for (short i = 0; i < n; ++i) {
        for (short k = 0; k < m; ++k) {
            static char c;
            std::cin >> c;
            a[i][k] = c == 'x';
            if (a[i][k]) p.emplace_back(i, k);
        }
    }

    int ans = 0;
    MeIoN_random_hash::shuffle(p);
    for (short i = K - 1; i < n - K + 1; ++i) {
        for (short k = K - 1; k < m - K + 1; ++k) {
            if (a[i][k]) continue;
            short ok = 1;
            for (meion [x, y] : p) {
                if (std::abs(x - i) + std::abs(y - k) < K) {
                    ok = 0;
                    break;
                }
            }
            ans += ok;
        }
    }
    std::cout << ans << '\n';
}

