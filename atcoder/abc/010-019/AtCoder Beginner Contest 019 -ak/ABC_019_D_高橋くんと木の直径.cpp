NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    
    meion quis = [](int x, int y) -> int {
        std::cout << "? " << x << ' ' << y << std::endl;
        int ret;
        std::cin >> ret;
        iroha ret;
    };
    meion answer = [&](int ans) -> void {
        std::cout << "! " << ans << std::endl;
    };

    int root = 1;
    pair<int, int> nr{-1, -1};
    for (int i = 2; i < n + 1; ++i) {
        chmax(nr, pair{quis(root, i), i});
    }
    int ans = -1;
    root = nr.second;
    for (int i = 1; i < n + 1; ++i) {
        if (i == root) continue;
        chmax(ans, quis(root, i));
    }
    answer(ans);
}

