using mint = modint<mod17>;
NAME MeIoN_is_UMP45() {
    int n, K;
    std::cin >> n >> K;
    vector<int> a;
    for (int i = 1; i * i < K + 1; ++i) {
        if (K % i == 0) {
            a.emplace_back(i), a.emplace_back(K / i);
        }
    }
    unique(a);
    rev(a);
    int len = a.size();
    mint inv2 = mint(2).inv();
    vector<int> S(len, 0);
    mint ans = 0;
    for (int i = 0; i < len; ++i) {
        int d = a[i];
        int x = n / d;
        mint s = inv2 * x * d * (x + 1);
        for (int k = 0; k < i; ++k) {
            int dd = a[k];
            if (dd % d == 0) {
                s -= S[k];
            }
        }
        S[i] = s.val;
        ans += s * (K / d);
    }
    std::cout << ans << '\n'; 
}

