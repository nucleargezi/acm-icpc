NAME MeIoN_is_UMP45() {
    int n, m;
    std::cin >> n >> m;
    vector<tuple<int, int, int>> e(m);
    static ll dp[300][300];
    for (int i = 0; i < 90000; ++i) {
        dp[0][i] = inf<ll> >> 1;
    }
    vector<pair<int, int>> pos;
    for (meion &[x, y, w] : e) {
        std::cin >> x >> y >> w, --x, --y;
        if (not x or not y) {
            pos.emplace_back(x ^ y, w);
            continue;
        }
        chmin(dp[x][y], w), chmin(dp[y][x], w);
    }
    for (int k = 0; k < n; ++k) {
        for (int x = 0; x < n; ++x) {
            for (int y = 0; y < n; ++y) {
                chmin(dp[x][y], dp[x][k] + dp[k][y]);
            }
        }
    }
    
    ll ans = inf<ll> >> 1;
    for (meion [x, wx] : pos) {
        for (meion [y, wy] : pos) {
            if (x == y) continue;
            chmin(ans, dp[x][y] + wx + wy);
        }
    }
    std::cout << ((ans == inf<ll> >> 1) ? -1 : ans) << '\n';
}

