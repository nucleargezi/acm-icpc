NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<P> a(n), b(n);
    std::cin >> a >> b;

    meion [x, y] = furthest_pair(a);
    RE s0 = P(a[x] - a[y]).square();
    
    std::tie(x, y) = furthest_pair(b);
    RE s1 = P(b[x] - b[y]).square();
    
    std::cout << std::sqrt(s1 / s0) << '\n';
}

