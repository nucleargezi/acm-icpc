NAME MeIoN_is_UMP45() {
    int n, m, K, sz;
    std::cin >> n >> m >> K >> sz;
    vector<vector<int>> v(n);
    vector<int> a(n), b(m);
    for (int i = 0, x, y; i < sz; ++i) {
        std::cin >> x >> y, --x, --y;
        ++a[x], ++b[y];
        v[x].emplace_back(y);
    }
    map<int, int> hs;
    for (int i = 0; i < m; ++i) {
        ++hs[b[i]];
    }
    ll ans = 0;
    for (int i = 0; i < n; ++i) {
        ans += hs[K - a[i]];
        for (int y : v[i]) {
            ans -= b[y] == K - a[i];
            ans += b[y] == K - a[i] + 1;
        }
    }
    std::cout << ans << '\n';
}

