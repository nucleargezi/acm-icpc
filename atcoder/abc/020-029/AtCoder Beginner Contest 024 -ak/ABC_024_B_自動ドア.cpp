NAME MeIoN_is_UMP45() {
    int n, t, pr;
    std::cin >> n >> t >> pr;
    int ans = t;
    for (int i = 1, x; i < n; ++i) {
        std::cin >> x;
        if (x - pr > t - 1) {
            ans += t;
        } else {
            ans += x - pr;
        }
        pr = x;
    }
    std::cout << ans << '\n';
}

