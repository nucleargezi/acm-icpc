NAME MeIoN_is_UMP45() {
    int n, d, k;
    std::cin >> n >> d >> k;
    vector<int> l(d), r(d);
    for (int i = 0; i < d; ++i) {
        std::cin >> l[i] >> r[i];
    }
    vector<int> s(k), t(k);
    for (int i = 0; i < k; ++i) {
        std::cin >> s[i] >> t[i];
    }
    vector<int> ans(k, -1);
    for (int a = 0; a < d; ++a) {
        for (int b = 0; b < k; ++b) {
            if (l[a] < s[b] + 1 and s[b] < r[a] + 1) {
                if (s[b] < t[b]) {
                    s[b] = std::min(r[a], t[b]);
                } else {
                    s[b] = std::max(l[a], t[b]);
                }
                if (ans[b] == -1 and s[b] == t[b]) {
                    ans[b] = a + 1;
                }
            }
        }
    }
    for (int i = 0; i < k; ++i) {
        std::cout << ans[i] << '\n';
    }
}

