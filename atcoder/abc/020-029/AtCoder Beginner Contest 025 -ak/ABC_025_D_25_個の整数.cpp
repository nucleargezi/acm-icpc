using mint = modint<mod17>;
NAME MeIoN_is_UMP45() {
    array<int, 25> v;
    v.fill(-1);

    for (int i = 0, x; i < 25; ++i) {
        std::cin >> x, --x;
        if (~x) {
            v[x] = i;
        }
    }

    static mint dp[1 << 25];
    dp[0] = 1;
    
    meion go = [&](int a, int b) -> void {
        int x = b / 5, y = b % 5;
        if (0 < x and x < 4 and (a >> b - 5 & 1) != (a >> b + 5 & 1))
            iroha;
        if (0 < y and y < 4 and (a >> b - 1 & 1) != (a >> b + 1 & 1))
            iroha;
        dp[a] += dp[a ^ (1 << b)];
    };
    
    for (int i = 1, x, y; i < (1 << 25); ++i) {
        int s = popcount(i) - 1;
        if (v[s] > -1) {
            go(i, v[s]);
        } else {
            for (int k = 0; k < 25; ++k) {
                if (i >> k & 1) {
                    go(i, k);
                }
            }
        }
    }

    std::cout << dp[(1 << 25) - 1] << '\n';
}

