NAME MeIoN_is_UMP45() {
    int n, a;
    string k;
    std::cin >> n >> a >> k, --a;
    vector<int> b(n);
    for (int i = 0; i < n; ++i) {
        std::cin >> b[i], --b[i];
    }

    vector<uint8_t> vis(n);
    vector<int> go;
    int s = a;
    while (not vis[s]) {
        go.emplace_back(s);
        vis[s] = 1;
        s = b[s];
    }

    int l = 0;
    while (go[l] != s) ++l;
    ll sum = 0, mod = go.size() - l;
    for (char c : k) {
        sum = (sum * 10 + c - '0') % mod;
    }

    string ss = std::to_string(l);
    if (k.size() < ss.size() or (k.size() == ss.size() and k < ss)) {
        std::cout << go[std::stoi(k)] + 1 << '\n';
    } else {
        std::cout << go[l + (sum + mod - l % mod) % mod] + 1 << '\n';
    }
}

