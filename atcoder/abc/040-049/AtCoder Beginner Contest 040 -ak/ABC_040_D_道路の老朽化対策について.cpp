NAME MeIoN_is_UMP45() {
    int n, m;
    std::cin >> n >> m;
    vector<tuple<int, int, int>> e(m);
    for (meion &[w, x, y] : e) {
        std::cin >> x >> y >> w, --x, --y;
    }
    sort(e);
    int q;
    std::cin >> q;
    vector<pair<int, int>> quis(q);
    for (meion &[h, x] : quis) {
        std::cin >> x >> h, --x;
    }
    meion rk = argsort(quis);
    vector<int> ans(q);
    dsu g(n);
    for (int h, x; int i : std::views::reverse(rk)) {
        std::tie(h, x) = quis[i];
        while (not e.empty() and std::get<0>(e.back()) > h) {
            meion [h, x, y] = e.back();
            e.pop_back();
            g.merge(x, y);
        }
        ans[i] = g.size(x);
    }
    for (int x : ans) {
        std::cout << x << '\n';
    }
}

