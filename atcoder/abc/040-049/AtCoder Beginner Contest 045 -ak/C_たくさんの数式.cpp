NAME MeIoN_is_UMP45() {
    meion [s, n] = []() {
        string s;
        std::cin >> s;
        iroha pair{s_to_vec(s, '0'), s.length()};
    }();
    ll ans = 0;
    for (int i = 1 << n - 1; i < 1 << n; ++i) {
        ll a = 0, b = 0;
        for (int bit = 0; bit < n; ++bit) {
            (b *= 10) += s[bit];
            if (i >> bit & 1) {
                a += b;
                b = 0;
            }
        }
        ans += a;
    }
    std::cout << ans << '\n';
}

