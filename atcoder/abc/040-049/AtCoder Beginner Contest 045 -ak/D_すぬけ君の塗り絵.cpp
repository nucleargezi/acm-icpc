NAME MeIoN_is_UMP45() {
    ll n, m, sz;
    std::cin >> n >> m >> sz;
    set<pair<int, int>> se, hs;
    for (int i = sz, x, y; i--; ) {
        std::cin >> x >> y;
        hs.emplace(x, y);
        for (int dx = -2; dx < 3; ++dx) {
            for (int dy = -2; dy < 3; ++dy) {
                if (x + dx > 0 and x + dx + 1 < n and y + dy > 0 and
                    y + dy + 1 < m) {
                    se.emplace(x + dx, y + dy);
                }
            }
        }
    }
    array<ll, 10> ans{};
    for (meion [x, y] : se) {
        int c = 0;
        for (int dx = 0; dx < 3; ++dx) {
            for (int dy = 0; dy < 3; ++dy) {
                c += hs.contains({x + dx, y + dy});
            }
        }
        ans[c] += not not c;
    }
    ans[0] = (n - 2) * (m - 2) - qsum(ans, 0ll);
    for (ll x : ans) {
        std::cout << x << '\n';
    }
}

