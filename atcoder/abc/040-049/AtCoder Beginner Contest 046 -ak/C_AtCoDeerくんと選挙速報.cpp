NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<pair<ll, ll>> v(n);
    std::cin >> v;
    ll a(0), b(0);
    for (meion [x, y] : v) {
        if (not a or not b) {
            std::tie(a, b) = pair{x, y};
        } else {
            ll M = binary_search([&](ll m) {
                iroha not(m * x < a or m * y < b);
            }, inf<ll> / std::max(x, y), 0);
            
            a = x * M, b = y * M;
        }
    }
    std::cout << a + b << '\n'; 
}

