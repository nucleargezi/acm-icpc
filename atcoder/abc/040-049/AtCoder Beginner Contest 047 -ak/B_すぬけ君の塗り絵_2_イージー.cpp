NAME MeIoN_is_UMP45() {
    int n, m, q;
    std::cin >> n >> m >> q;
    bool v[n][m];
    memset(v, 0, sizeof v);
    for (int i = q, x, y, a; i--; ) {
        std::cin >> x >> y >> a;
        if (a == 1) {
            for (int i = 0; i < x; ++i) {
                for (int k = 0; k < m; ++k) {
                    v[i][k] = 1;
                }
            }
        } else if (a == 2) {
            for (int i = x; i < n; ++i) {
                for (int k = 0; k < m; ++k) {
                    v[i][k] = 1;
                }
            }
        } else if (a == 3) {
            for (int i = 0; i < n; ++i) {
                for (int k = 0; k < y; ++k) {
                    v[i][k] = 1;
                }
            }
        } else {
            for (int i = 0; i < n; ++i) {
                for (int k = y; k < m; ++k) {
                    v[i][k] = 1;
                }
            }
        }
    }
    int ans = n * m;
    for (int i = 0; i < (n * m); ++i) {
        ans -= v[0][i];
    }
    std::cout << ans << '\n';
}

