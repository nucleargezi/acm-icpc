NAME MeIoN_is_UMP45() {
    int n, k, l;
    std::cin >> n >> k >> l;
    dsu a(n), b(n);
    for (int i = k, x, y; i--; ) {
        std::cin >> x >> y, --x, --y;
        a.merge(x, y);
    }
    for (int i = l, x, y; i--; ) {
        std::cin >> x >> y, --x, --y;
        b.merge(x, y);
    }
    map<int, vector<int>> S;
    for (int i = 0; i < n; ++i) {
        S[a[i]].emplace_back(i);
    }
    vector<int> ans(n);
    for (const meion &[f, v] : S) {
        map<int, int> id;
        for (int i : v) {
            if (not id.contains(b[i])) {
                id[b[i]] = i;
            }
            ans[id[b[i]]]++;
        }
        for (int i : v) {
            ans[i] = ans[id[b[i]]];
        }
    }
    std::cout << ans << '\n';
}

