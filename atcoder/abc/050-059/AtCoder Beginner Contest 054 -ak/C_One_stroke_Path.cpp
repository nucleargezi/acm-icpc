NAME MeIoN_is_UMP45() {
    int n, m;
    std::cin >> n >> m;
    bool vis[n][n];
    memset(vis, 0, sizeof vis);
    for (int i = 0, x, y; i < m; ++i) {
        std::cin >> x >> y, --x, --y;
        vis[x][y] = vis[y][x] = 1;
    }
    vector<array<int, 8>> dp(1 << n, array<int, 8>{});
    dp[1][0] = 1;
    for (int i = 1; i < (1 << n); ++i) {
        for (int nxt = 0; nxt < n; ++nxt) {
            if (i >> nxt & 1) continue;
            for (int hs = 0; hs < n; ++hs) {
                if (not(i >> hs & 1) or not(vis[hs][nxt]) or not dp[i][hs]) continue;
                dp[1 << nxt | i][nxt] += dp[i][hs];
            }
        }
    }
    std::cout << qsum(dp.back(), 0) << '\n';
}

