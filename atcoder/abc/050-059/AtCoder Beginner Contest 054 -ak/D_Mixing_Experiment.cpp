NAME MeIoN_is_UMP45() {
    int n, A, B;
    std::cin >> n >> A >> B;
    int N = 0721;
    int dp[N][N];
    memset(dp, 0, sizeof dp);
    for (int i = 0, a, b, c; i < n; ++i) {
        std::cin >> a >> b >> c;
        for (int k = N; k--; ) {
            for (int j = N; j--; ) {
                if (not k and not j) {
                    if (dp[a][b]) {
                        chmin(dp[a][b], c);
                    } else {
                        dp[a][b] = c;
                    }
                } else if (dp[k][j]) {
                    if (dp[k + a][j + b]) {
                        chmin(dp[k + a][j + b], dp[k][j] + c);
                    } else {
                        dp[k + a][j + b] = dp[k][j] + c;
                    }
                }
            }
        }
    }
    int ans = inf<int> ;
    for (int i = 1; i < (N - 1) / std::max(A, B) + 1; ++i) {
        if (dp[A * i][B * i]) {
            chmin(ans, dp[A * i][B * i]);
        }
    }
    std::cout << (inf<int> == ans ? -1 : ans) << '\n';
}

