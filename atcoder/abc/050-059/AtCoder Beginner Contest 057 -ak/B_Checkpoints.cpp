NAME MeIoN_is_UMP45() {
    int n, m;
    std::cin >> n >> m;
    int x[n], y[n];
    for (int i = 0; i < n; ++i) {
        std::cin >> x[i] >> y[i];
    }
    int px[m], py[m];
    for (int i = 0; i < m; ++i) {
        std::cin >> px[i] >> py[i];
    }
    for (int i = 0; i < n; ++i) {
        pair<int, int> get(inf<int>, -1);
        for (int k = 0; k < m; ++k) {
            chmin(get, pair(std::abs(x[i] - px[k]) + std::abs(y[i] - py[k]), k));
        }
        std::cout << get.second + 1 << '\n';
    } 
}

