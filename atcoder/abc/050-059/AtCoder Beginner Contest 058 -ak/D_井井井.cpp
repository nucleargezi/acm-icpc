using mint = modint<mod17>;
NAME MeIoN_is_UMP45() {
    int n, m;
    std::cin >> n >> m;
    vector<ll> a(n), b(m);
    std::cin >> a >> b;
    mint sa = 0, sb = 0;
    for (int i = 1; i < n; ++i) {
        sa += (a[i] - a[i - 1]) * i * (n - i);
    }
    for (int i = 1; i < m; ++i) {
        sb += (b[i] - b[i - 1]) * i * (m - i);
    }
    std::cout << sa * sb << '\n';
}

