NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    ll ans = inf<ll>;
    vector<int> a(n);
    std::cin >> a;
    ll s = 0, r = 0;
    for (int i = 0; i < n; ++i) {
        s += a[i];
        if ((i & 1)) {
            if (s > 0) {
                r += s + 1;
                s = -1;
            } else if (not s) {
                s = -1;
                ++r;
            }
        }
        if (not(i & 1)) {
            if (s < 0) {
                r += -s + 1;
                s = 1;
            } else if (not s) {
                s = 1;
                ++r;
            }
        }
    }
    chmin(ans, r);
    s = 0, r = 0;
    for (int i = 0; i < n; ++i) {
        s += a[i];
        if (not (i & 1)) {
            if (s > 0) {
                r += s + 1;
                s = -1;
            } else if (not s) {
                s = -1;
                ++r;
            }
        }
        if ((i & 1)) {
            if (s < 0) {
                r += -s + 1;
                s = 1;
            } else if (not s) {
                s = 1;
                ++r;
            }
        }
    }
    chmin(ans, r);
    std::cout << ans << '\n';
}

