NAME MeIoN_is_UMP45() {
    int n, w;
    std::cin >> n >> w;
    vector<ll> val[4];
    int sz[4];
    ll min = inf<ll>;
    for (int i = 0, w, v; i < n; ++i) {
        std::cin >> w >> v;
        chmin(min, w);
        val[w - min].emplace_back(v);
    }
    for (int i = 0; i < 4; ++i) {
        sort(val[i], greater());
        sz[i] = val[i].size();
        val[i] = pre_sum(val[i]);
    }
    ll ans = -inf<ll>;
    for (int a = 0; a < n + 1 and a < sz[0] + 1; ++a) {
        for (int b = 0; b < n + 1 - a and b < sz[1] + 1; ++b) {
            for (int c = 0; c < n + 1 - a - b and c < sz[2] + 1; ++c) {
                ll cost = min * a + (min + 1) * b + (min + 2) * c;
                if (cost > w) continue;
                int d = n - a - b - c;
                chmin(d, (w - cost) / (min + 3));
                chmin(d, sz[3]);
                chmax(ans, val[0][a] + val[1][b] + val[2][c] + val[3][d]);
            }
        }
    }
    std::cout << ans << '\n';
}

