NAME MeIoN_is_UMP45() {
    int n, m;
    std::cin >> n >> m;
    vector<pair<int, int>> v[n];
    for (int i = 0, a, b, c; i < m; ++i) {
        std::cin >> a >> b >> c, --a, --b;
        v[a].emplace_back(b, -c);
    }
    bitset<1000> vis, inc;
    vis.reset(), inc.reset();
    vector<ll> dis(n, inf<ll>);
    vector<int> in(n);
    dis[0] = 0, vis[0] = 1;
    queue<int> q;
    q.emplace_back(0);
    while (not q.empty()) {
        int al = n;
        int n = q.front();
        q.pop();
        vis[n] = 0;
        for (meion [i, w] : v[n]) {
            if (inc[i]) continue;
            if (chmin(dis[i], dis[n] + w)) {
                if (not vis[i]) {
                    vis[i] = 1;
                    q.emplace_back(i);
                    if (++in[i] > al - 1) {
                        inc[i] = 1;
                    }
                }
            }
        }
    }
    if (inc[n - 1]) {
        std::cout << "inf\n";
    } else {
        std::cout << -dis[n - 1] << '\n';
    }
}

