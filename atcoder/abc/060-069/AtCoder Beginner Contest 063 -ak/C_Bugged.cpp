NAME MeIoN_is_UMP45() {
    static bitset<10001> dp;
    dp[0] = 1;
    int n;
    std::cin >> n;
    for (int i = 0, x; i < n; ++i) {
        std::cin >> x;
        dp |= dp << x;
    }
    int ans = 0;
    for (int i = 0; i < 10001; ++i) {
        if (dp[i] and i % 10) {
            ans = i;
        }
    }
    std::cout << ans << '\n';
}

