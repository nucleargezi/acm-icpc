NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<pair<int, int>> ps(n);
    std::cin >> ps;
    vector<vector<pair<int, int>>> v(n);
    meion add = [&](int x, int y) -> void {
        meion [a, b] = ps[x];
        meion [c, d] = ps[y];
        int w = std::min(std::abs(a - c), std::abs(b - d));
        v[x].emplace_back(y, w);
        v[y].emplace_back(x, w);
    };
    for (int i = 0; i < 2; ++i) {
        vector rk = argsort(ps);
        for (int i = 1; i < n; ++i) {
            add(rk[i - 1], rk[i]);
        }
        for_each(ps, [](pair<int, int> &x) -> void {
            std::swap(x.first, x.second);
        });
    }
    priority_queue<pair<ll, int>> q;
    static bitset<1'000'00> vis;
    q.emplace(0, 0);
    ll ret = 0;
    while (not q.empty()) {
        const meion [val, n] = q.top();
        q.pop();
        if (vis[n]) continue;
        vis[n] = 1;
        ret -= val;
        for (const meion &[i, val] : v[n]) {
            q.emplace(-val, i);
        }
    }
    std::cout << ret << '\n';
}

