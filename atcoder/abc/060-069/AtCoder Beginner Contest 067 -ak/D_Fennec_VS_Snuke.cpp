NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<vector<int>> v(n);
    for (int i = 1, x, y; i < n; ++i) {
        std::cin >> x >> y, --x, --y;
        v[x].emplace_back(y), v[y].emplace_back(x);
    }
    LCA<17> lca(v, 0);
    int dis = lca.dist(0, n - 1);
    int p = dis > 2 ? lca.jump(n - 1, (dis - 1 >> 1) - 1) : n - 1;
    int s = 0, fa = lca.jump(p, 0);
    meion ser = [&](meion &&ser, int n, int fa) -> void {
        ++s;
        for (int i : v[n]) {
            if (i == fa) continue;
            ser(ser, i, n);
        }
    };
    ser(ser, p, fa);
    std::cout << (n - s < s + 1 ? "Snuke" : "Fennec") << '\n';
}

