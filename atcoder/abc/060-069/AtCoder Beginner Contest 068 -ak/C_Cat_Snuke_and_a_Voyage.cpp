NAME MeIoN_is_UMP45() {
    int n, m;
    std::cin >> n >> m;
    set<int> se[n];
    for (int i = 0, x, y; i < m; ++i) {
        std::cin >> x >> y, --x, --y;
        se[x].emplace(y), se[y].emplace(x);
    }
    int ok = 0;
    for (int i = 0; i < n and not ok; ++i) {
        se[i].emplace(i);
        ok |= se[i].contains(0) and se[i].contains(n - 1);
    }
    POSSIBLE(ok);
}

