NAME MeIoN_is_UMP45() {
    ll k;
    std::cin >> k;
	ll n = 50;
	ll x = k / n, r = k % n;
	ll a = (n - 1 + x) + n - r + 1, b = (n - 1 + x) - r;
	string ans;
	for (int i = r; i--; ) ans += std::to_string(a) + ' ';
	for (int i = n - r; i--; ) ans += std::to_string(b) + ' ';
	ans.pop_back();
	std::cout << n << '\n' << ans << '\n';
}

