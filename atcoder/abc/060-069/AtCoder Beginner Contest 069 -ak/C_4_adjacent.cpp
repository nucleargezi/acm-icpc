NAME MeIoN_is_UMP45() {
    int c[3] = {};
    int n;
    std::cin >> n;
    for (int i = 0, x; i < n; ++i) {
        std::cin >> x;
        if (x & 1) ++c[0];
        else if (x % 4 == 0) ++c[2];
        else ++c[1];
    }
    Yes((c[1] / 2 + c[2]) * 2 > n / 2 * 2 - 1);
}

