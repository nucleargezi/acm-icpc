NAME MeIoN_is_UMP45() {
    int n, m;
    std::cin >> n >> m;
    vector<int> res;
    int col;
    std::cin >> col;
    for (int i = 0, sz; i < col; ++i) {
        std::cin >> sz;
        while (sz--) res.emplace_back(i + 1);
    }
    for (int i = 0; i < n; ++i) {
        vector<int> out{res.begin() + i * m, res.begin() + i * m + m};
        if (i & 1) rev(out);
        std::cout << out << '\n';
    }
}

