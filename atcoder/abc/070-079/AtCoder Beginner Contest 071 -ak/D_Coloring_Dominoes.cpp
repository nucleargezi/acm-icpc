using mint = modint<mod17>;
NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    string s[2];
    mint dp[n];
    std::cin >> s[0] >> s[1];
    for (int i = 0; i < n; ++i) {
        if (not i) {
            if (s[0][0] == s[1][0]) dp[0] = 3;
            else dp[0] = dp[1] = 6, ++i;
        } else {
            if (s[0][i] == s[1][i]) {
                if (s[0][i - 1] == s[1][i - 1]) {
                    dp[i] = dp[i - 1] * 2;
                } else {
                    dp[i] = dp[i - 1];
                }
            } else {
                if (s[0][i - 1] == s[1][i - 1]) {
                    dp[i] = dp[i + 1] = dp[i - 1] * 2;
                    ++i;
                } else {
                    dp[i + 1] = dp[i] = dp[i - 1] * 3;
                    ++i;
                }
            }
        }
    }
    std::cout << dp[n - 1] << '\n';
}

