NAME MeIoN_is_UMP45() {
    int n, m, sz;
    std::cin >> n >> m >> sz;
    vector<int> id(sz);
    for (meion &x : id) {
        std::cin >> x, --x;
    }
    vector<tuple<int, int, ll>> e(m);
    for (meion &[x, y, w] : e) {
        std::cin >> x >> y >> w;
        --x, --y;
    }
    array dis = floyd<200>(e, n);
    vector<array<ll, 8>> dp(1 << sz);
    for (meion &x : dp) x.fill(inf<ll>);
    for (int i = 1; i < (1 << sz); ++i) {
        if (popcount(i) == 1) {
            dp[i][topbit(i)] = 0;
        } else {
            for (int bit = 0; bit < sz; ++bit) {
                if (not(i >> bit & 1)) continue;
                for (int nxt = 0; nxt < sz; ++nxt) {
                    if (not(i >> nxt & 1) or nxt == bit) continue;
                    chmin(dp[i][nxt],
                          dp[1 << nxt ^ i][bit] + dis[id[bit]][id[nxt]]);
                }
            }
        }
    }
    std::cout << qmin(dp.back()) << '\n';
}

