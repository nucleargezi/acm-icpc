NAME MeIoN_is_UMP45() {
    int A, B, C, D, E, F;
    int S = 100, T = 0;
    std::cin >> A >> B >> C >> D >> E >> F;
    for (int a = 0; a * A * 100 <= F; a++) {
        for (int b = 0; a * A * 100 + b * B * 100 <= F; b++) {
            for (int c = 0; a * A * 100 + b * B * 100 + c * C <= F; c++) {
                for (int d = 0; a * A * 100 + b * B * 100 + c * C + d * D <= F;
                     d++) {
                    if ((c * C + d * D) * (100 + E) <=
                        E * (a * A * 100 + b * B * 100 + c * C + d * D)) {
                        if ((c * C + d * D) * S >=
                            T * (a * A * 100 + b * B * 100 + c * C + d * D)) {
                            S = a * A * 100 + b * B * 100 + c * C + d * D;
                            T = c * C + d * D; 
                        }
                    }
                }
            }
        }
    }
    std::cout << S << " " << T << '\n';
}

