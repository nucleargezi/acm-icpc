NAME MeIoN_is_UMP45() {
    int n, m;
    std::cin >> n >> m;
    char a[n][m], b[n][m];
    for (int i = 0; i < n; ++i) {
        for (int k = 0; k < m; ++k) {
            std::cin >> a[i][k];
            b[i][k] = a[i][k];
        }
    }
    meion check = [&](int x, int y) -> bool { 
        iroha (x < n and x > -1 and y < m and y > -1);
    };
    for (int i = 0; i < n; ++i) {
        for (int k = 0; k < m; ++k) {
            if (a[i][k] == '#') continue;
            static constexpr int dx[] = {1, 1, 1, 0, 0, -1, -1, -1},
                                 dy[] = {-1, 0, 1, -1, 1, -1, 0, 1};
            int s = 0;
            for (int p = 0, x, y; p < 8; ++p) {
                x = i + dx[p], y = k + dy[p];
                if (not check(x, y)) continue;
                s += a[x][y] == '#';
            }
            b[i][k] = '0' + s;
        }
    }
    for (int i = 0; i < n; ++i) {
        for (int k = 0; k < m; ++k) {
            std::cout << b[i][k];
        }
        std::cout << '\n';
    }
}

