NAME MeIoN_is_UMP45() {
    using P = pair<ll, ll>;
    int n, K;
    std::cin >> n >> K;
    vector<P> ps(n);
    std::cin >> ps;
    ll ans = inf<ll>;
    for (int i = 0; i < n; ++i) {
        for (int k = i + 1; k < n; ++k) {
            for (int j = k + 1; j < n; ++j) {
                for (int l = j + 1; l < n; ++l) {
                    ll lx = std::min({ps[i].first, ps[k].first, ps[j].first,
                                      ps[l].first}),
                       ly = std::min({ps[i].second, ps[k].second, ps[j].second,
                                      ps[l].second});
                    ll rx = std::max({ps[i].first, ps[k].first, ps[j].first,
                                      ps[l].first}),
                       ry = std::max({ps[i].second, ps[k].second, ps[j].second,
                                      ps[l].second});
                    int cnt = 0;
                    for (int i = 0; i < n; ++i) {
                        cnt +=
                            (lx - 1 < ps[i].first and rx + 1 > ps[i].first and
                             ly - 1 < ps[i].second and ry + 1 > ps[i].second);
                    }
                    if (cnt > K - 1) chmin(ans, (rx - lx) * (ry - ly));
                }
            }
        }
    }
    for (int i = 0; i < n; ++i) {
        for (int k = i + 1; k < n; ++k) {
            for (int j = k + 1; j < n; ++j) {
                ll lx = std::min({ps[i].first, ps[k].first, ps[j].first}),
                   ly = std::min({ps[i].second, ps[k].second, ps[j].second});
                ll rx = std::max({ps[i].first, ps[k].first, ps[j].first}),
                   ry = std::max({ps[i].second, ps[k].second, ps[j].second});
                int cnt = 0;
                for (int i = 0; i < n; ++i) {
                    cnt += (lx - 1 < ps[i].first and rx + 1 > ps[i].first and
                            ly - 1 < ps[i].second and ry + 1 > ps[i].second);
                }
                if (cnt > K - 1) chmin(ans, (rx - lx) * (ry - ly));
            }
        }
    }
    for (int i = 0; i < n; ++i) {
        for (int k = i + 1; k < n; ++k) {
            ll lx = std::min({ps[i].first, ps[k].first}),
               ly = std::min({ps[i].second, ps[k].second});
            ll rx = std::max({ps[i].first, ps[k].first}),
               ry = std::max({ps[i].second, ps[k].second});
            int cnt = 0;
            for (int i = 0; i < n; ++i) {
                cnt += (lx - 1 < ps[i].first and rx + 1 > ps[i].first and
                        ly - 1 < ps[i].second and ry + 1 > ps[i].second);
            }
            if (cnt > K - 1) chmin(ans, (rx - lx) * (ry - ly));
        }
    }
    std::cout << ans << '\n';
}

