NAME MeIoN_is_UMP45() {
    string s, t;
    std::cin >> s >> t;
    const int n = s.length(), m = t.length();
    set<string> se;
    for (int i = 0; i < n - m + 1; ++i) {
        int ok = 1;
        for (int k = 0; k < m and ok; ++k) {
            ok &= s[i + k] == '?' or s[i + k] == t[k];
        }
        if (ok) {
            se.emplace([&]() -> string{
                string st = s;
                for (int k = 0; k < m and ok; ++k) {
                    st[i + k] = t[k];
                }
                for (int i = 0; i < n; ++i) {
                    if (st[i] == '?') st[i] = 'a';
                }
                iroha st;
            }());
        }
    }
    std::cout << (se.empty() ? "UNRESTORABLE" : *se.begin()) << '\n';
}

