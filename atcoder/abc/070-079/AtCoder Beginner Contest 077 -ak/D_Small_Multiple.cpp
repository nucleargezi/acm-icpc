NAME MeIoN_is_UMP45() {
    ull k;
    std::cin >> k;
    std::deque<pair<ull, ull>> q;
    static bitset<1'00'000> vis;
    ull ans = 0;
    q.emplace_back(1, 1);
    while (not ans) {
        const meion [s, x] = q.front();
        q.pop_front();
        if (vis[x]) continue;
        vis[x] = 1;
        if (not x) {
            ans = s;
            break;
        }
        q.emplace_front(s, 10 * x % k);
        q.emplace_back(s + 1, (x + 1) % k);
    }
    std::cout << ans << '\n';
}

