NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector a(n, array<int, 10>{});
    vector p(n, array<int, 11>{});
    std::cin >> a >> p;
    ll ans{-inf<ll>};
    for (int msk{1}; msk < (1 << 10); ++msk) {
        ll s{};
        for (int i{}; i < n; ++i) {
            int cnt{};
            for (int bit{}; bit < 10; ++bit) {
                if (msk >> bit & 1) {
                    cnt += a[i][bit];
                }
            }
            s += p[i][cnt];
        }
        chmax(ans, s);
    }
    std::cout << ans << '\n';
}

