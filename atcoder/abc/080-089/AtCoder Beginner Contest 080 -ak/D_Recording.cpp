NAME MeIoN_is_UMP45() {
    int n, C;
    std::cin >> n >> C;
    vector v(C, vector<int>(1'000'01));
    for (int i{}, l, r, w; i < n; ++i) {
        std::cin >> l >> r >> w;
        --l, --w;
        ++v[w][l], --v[w][r];
    }
    for (meion &x : v) {
        x = pre_sum(x);
    }
    int ans{};
    for (int i{}; i < 1'000'02; ++i) {
        int c{};
        for (int k{}; k < C; ++k) {
            c += not not v[k][i];
        }
        chmax(ans, c);
    }
    std::cout << ans << '\n';
}

