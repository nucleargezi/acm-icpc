NAME MeIoN_is_UMP45() {
    string s;
    int px, py;
    std::cin >> s >> px >> py;
    if (s.back() != 'T') s += 'T';
    int f = 0;
    vector<int> a[2];
    for (int cnt{}; char c : s) {
        if (c == 'F') {
            ++cnt;
        } else if (c == 'T') {
            a[f].emplace_back(cnt);
            cnt = 0;
            f ^= 1;
        }
    }
    int sum[] = {qsum(a[0], 0), qsum(a[1], 0)};
    if (((std::abs(px) & 1) != (sum[0] & 1)) or
        ((std::abs(py) & 1) != (sum[1] & 1))) {
        iroha No();
    }
    int x{sum[0] + px >> 1}, y{sum[1] + py >> 1};
    static bitset<0721 << 6> dp, dpp;
    if (a[0].size()) {
        dp[a[0][0]] = 1;
    }
    for (int i{1}; i < a[0].size(); ++i) {
        dp |= dp << a[0][i];
    }
    dpp[0] = 1;
    for (int i{}; i < a[1].size(); ++i) {
        dpp |= dpp << a[1][i];
    }
    Yes(x < dp.size() and y < dpp.size() and dp[x] and dpp[y]);
}

