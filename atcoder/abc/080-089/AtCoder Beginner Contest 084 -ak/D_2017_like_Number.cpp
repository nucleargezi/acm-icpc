constexpr int N = 1'000'01;
int c[N];
void before() {
    std::cin.tie(nullptr)->sync_with_stdio(false);
    std::cout << std::fixed << std::setprecision(12);
    for (int i{3}; i < N; ++i) {
        if (primetest(i) and primetest(i + 1 >> 1)) {
            c[i] = 1;
        }
    }
    for (int i{3}; i < N; ++i) {
        c[i] += c[i - 1];
    }
}
