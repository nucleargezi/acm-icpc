NAME MeIoN_is_UMP45() {
    int n, h;
    std::cin >> n >> h;
    vector<ll> c(n);
    ll hit{-inf<ll>};
    for (int i{}, x; i < n; ++i) {
        std::cin >> x >> c[i];
        chmax(hit, x);
    }
    sort(c, greater());
    c = pre_sum(c);
    ll ans{inf<ll>};
    for (int i = 0; i < n + 1; ++i) {
        chmin(ans, i + ceil(std::max(h - c[i], 0ll), hit));
    }
    std::cout << ans << '\n';
}

