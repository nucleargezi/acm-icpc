NAME MeIoN_is_UMP45() {
    int n, k;
    std::cin >> n >> k;
    const int k2 = k + k;
    int a[k][k];
    memset(a, 0, sizeof a);
    for (int i{}, x, y; i < n; ++i) {
        char dir;
        std::cin >> x >> y >> dir;
        x %= k2, y %= k2;
        dir = dir == 'W';
        if (not(x < k)) x -= k, dir ^= 1;
        if (not(y < k)) y -= k, dir ^= 1;
        if (not dir) {
            a[0][0] += 1, a[x][y] += 2;
            a[x][0] -= 1, a[0][y] -= 1;
        } else {
            a[x][0] += 1, a[0][y] += 1;
            a[x][y] -= 1, a[x][y] -= 1;
        }
    }
    for (int x{0}; x < k; ++x) {
        for (int y{0}; y < k; ++y) {
            if (y) a[x][y] += a[x][y - 1];
            if (x) a[x][y] += a[x - 1][y];
            if (x and y) a[x][y] -= a[x - 1][y - 1];
        }
    }
    int ans{};
    for (int i = 0; i < k * k; ++i) {
        chmax(ans, std::max(a[0][i], n - a[0][i]));
    }
    std::cout << ans << '\n';
}

