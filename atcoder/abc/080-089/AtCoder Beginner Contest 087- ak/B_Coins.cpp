NAME MeIoN_is_UMP45() {
    int a, b, c, x;
    std::cin >> a >> b >> c >> x;
    int ans{};
    for (int i{}; i * 500 < x + 1 and i < a + 1; ++i) {
        for (int k{}; i * 500 + k * 100 < x + 1 and k < b + 1; ++k) {
            ans += not((x - i * 500 - k * 100) % 50) and
                   (x - i * 500 - k * 100) / 50 < c + 1;
        }
    }
    std::cout << ans << '\n';
}

