NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    map<char, set<pair<ll, ll>>> m;
    for (int i{}; i < n; ++i) {
        string s;
        std::cin >> s;
        m[s[0]].emplace(HASH(s).get(0, s.length()));
    }
    ull ans{};
    const vector<char> c {'M', 'A', 'R', 'C', 'H'};
    for (int i{}; i < (1 << 5); ++i) {
        if (popcount(i) != 3) continue;
        vector<ull> sz;
        for (int bit{}; bit < 5; ++bit) {
            if (i >> bit & 1) sz.emplace_back(m[c[bit]].size());
        }
        ans += sz[0] * sz[1] * sz[2];
    }
    std::cout << ans << '\n';
}

