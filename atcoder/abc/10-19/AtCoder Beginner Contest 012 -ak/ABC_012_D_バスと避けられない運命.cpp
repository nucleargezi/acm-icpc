NAME MeIoN_is_UMP45() {
    int n, m;
    std::cin >> n >> m;
    static int dp[300][300];
    for (int i = 0; i < n; ++i) {
        for (int k = 0; k < n; ++k) {
            dp[i][k] = inf<int> >> 10;
        }
    }
    for (int x, y, w, i = 0; i < m; ++i) {
        std::cin >> x >> y >> w, --x, --y;
        dp[x][y] = dp[y][x] = w;
    }
    for (int k = 0; k < n; ++k) {
        for (int x = 0; x < n; ++x) {
            for (int y = 0; y < n; ++y) {
                chmin(dp[x][y], dp[x][k] + dp[k][y]);
            }
        }
    }

    int ans = inf<int>;
    for (int i = 0; i < n; ++i) {
        int max = -inf<int>;
        for (int k = 0; k < n; ++k) {
            if (i == k) continue;
            chmax(max, dp[i][k]);
        }
        chmin(ans, max);
    }
    std::cout << ans << '\n';
}

