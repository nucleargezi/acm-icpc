NAME MeIoN_is_UMP45() {
    int n, m, d;
    std::cin >> n >> m >> d;
    vector<int> a(m);
    std::cin >> a;
    for_each(a, [](int &x) { --x; });
    vector<int> f(n);
    std::iota(f.begin(), f.end(), 0);
    for (int i = 0; i < m; ++i) {
        std::swap(f[a[i]], f[a[i] + 1]);
    }
    vector<array<int, 30>> up(n);
    for (int i = 0; i < n; ++i) {
        up[f[i]][0] = i; 
    }
    for (int k = 1; k < 30; ++k) {
        for (int i = 0; i < n; ++i) {
            up[i][k] = up[up[i][k - 1]][k - 1];
        }
    }
    for (int i = 0, x; i < n; ++i) {
        x = i;
        for (int i = 30; i--; ) {
            if (d >> i & 1) {
                x = up[x][i];
            }
        }
        std::cout << ++x << '\n';
    }
}

