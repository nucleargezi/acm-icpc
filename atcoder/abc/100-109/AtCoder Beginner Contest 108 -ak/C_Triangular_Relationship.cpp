NAME MeIoN_is_UMP45() {
    int n, k;
    std::cin >> n >> k;
    ll ans = n / k;
    ans *= ans * ans;
    if (not(k & 1)) {
        ll t = (n + k / 2) / k;
        ans += t * t * t;
    }
    std::cout << ans << '\n';
}

