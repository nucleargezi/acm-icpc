NAME MeIoN_is_UMP45() {
    int n, x;
    std::cin >> n >> x;
    int ans{};
    for (int i{}, y; i < n; ++i) {
        std::cin >> y;
        ans = std::gcd(ans, ABS(x - y));
    }
    std::cout << ans << '\n';
}

