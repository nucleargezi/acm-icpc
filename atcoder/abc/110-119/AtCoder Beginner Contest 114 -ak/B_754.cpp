NAME MeIoN_is_UMP45() {
    string s;
    std::cin >> s;
    const int n = int(s.length());
    int ans{inf<int>};
    for (int i{}; i < n - 2; ++i) {
        chmin(ans, ABS(std::stoi(s.substr(i, 3)) - 753));
    }
    std::cout << ans << '\n';
}

