NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<int> a(n);
    std::cin >> a;
    int ans{};
    for (int i{1}; i < n - 1; ++i) {
        meion [l, r] = std::minmax({a[i - 1], a[i], a[i + 1]});
        ans += l < a[i] and r > a[i];
    }
    std::cout << ans << '\n';
}

