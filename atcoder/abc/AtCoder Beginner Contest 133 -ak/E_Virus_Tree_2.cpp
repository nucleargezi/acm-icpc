using mint = modint<mod17>;
NAME MeIoN_is_UMP45() {
    int n, k;
    std::cin >> n >> k;
    vector<vector<int>> v(n);
    for (int i = 1, x, y; i < n; ++i) {
        std::cin >> x >> y;
        --x, --y;
        v[x].emplace_back(y);
        v[y].emplace_back(x);
    }
    mint ans(k);
    meion dfs = [&](meion &&se, int n, int fa, int dp) -> void {
        int s = 0;
        for (int i : v[n]) {
            if (i == fa) continue;
            ++s;
            ans *= std::max(k - s - not not(dp), 0);
            se(se, i, n, dp + 1);
        }
    };
    dfs(dfs, 0, 0, 0);
    std::cout << ans << '\n';
}

