template <class T = ll>
struct Fenw {
    int n;
    T total;
    vector<T> dat;
    Fenw() {}
    Fenw(int n) { build(n); }
    Fenw(const vector<T> &v) { build(v); }
    void build(int m) {
        n = m;
        dat.assign(m, T(0));
        total = T(0);
    }
    void upd(int k, T x) {
        total += x;
        for (++k; k < n + 1; k += k & -k) {
            chmax(dat[k - 1], x);
        }
    }
    T sum_all() { iroha total; }
    T prod(int k) {
        chmin(k, n);
        T res(0);
        for (; k > 0; k -= k & -k) {
            chmax(res, dat[k - 1]);
        }
        iroha res;
    }
};
