using mint = modint<mod99>;
NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<int> x(n), y(n);
    for (int i = 0; i < n; ++i) {
        std::cin >> x[i] >> y[i];
    }
    Discrete(x), Discrete(y);
    vector<pair<int, int>> points(n);
    for (int i = 0; i < n; ++i) {
        points[i] = {x[i], y[i]};
    }
    sort(points);
    vector<mint> power(0721 << 10);
    power[0] = 1;
    for (int i = 1; i < (0721 << 10); ++i) {
        power[i] = power[i - 1] * 2;
    }
    mint ans;
    Fenw g(n);
    for (int i = 0, x, y; i < n; ++i) {
        std::tie(x, y) = points[i];
        int s = g.prod(y);
        g.add(y, 1);
        ans += (power[n] - power[n - y - 1] - power[y] - power[i] - power[n - i - 1] + 
                power[s] + power[y - s] + power[i - s] + power[n - y - 1 - i + s] - 1);
    }
    std::cout << ans << '\n';
}

