NAME MeIoN_is_UMP45() {
    int mod;
    std::cin >> mod;
    vector<int> a(mod), b(mod);
    std::cin >> a;
    b[0] = a[0];
    for (int i = 0; i < mod - 1; ++i) {
        for (int k = 0; k < mod; ++k) {
            b[mod - 1 - i] = (b[mod - 1 - i] + a[k]) % mod;
            a[k] = a[k] * k % mod;
        }
    }
    for (int i = 1; i < mod; ++i) {
        b[i] = b[i] * (mod - 1) % mod;
    }
    std::cout << b << '\n';
}

