NAME MeIoN_is_UMP45() {
    int n, m;
    std::cin >> n >> m;
    vector<int> a(n), b(n), c(n), d(m), e(m), f(m);
    map<int, int> ma{{0, 0}}, mb{{0, 0}};
    for (int i = 0; i < n; ++i) {
        std::cin >> a[i] >> b[i] >> c[i];
        ma[a[i]], ma[b[i]], mb[c[i]];
    } 
    for (int i = 0; i < m; ++i) {
        std::cin >> d[i] >> e[i] >> f[i];
        ma[d[i]], mb[e[i]], mb[f[i]];
    }
    for (int i = 0; meion &[x, y] : ma) {
        y = i++;
    }
    for (int i = 0; meion &[x, y] : mb) { 
        y = i++;
    }

    vector<int> u, v;
    static int h[0721 << 4][0721 << 4], w[0721 << 4][0721 << 4],
        vis[0721 << 4][0721 << 4];
    for (meion [x, y] : ma) u.emplace_back(x);
    for (meion [x, y] : mb) v.emplace_back(x);
    for (int i = 0; i < n; ++i) {
        for (int k = ma[a[i]], ed = ma[b[i]]; k < ed; ++k) {
            h[k][mb[c[i]]] = 1;
        }
    }
    for (int i = 0; i < m; ++i) {
        for (int k = mb[e[i]], ed = mb[f[i]]; k < ed; ++k) {
            w[ma[d[i]]][k] = 1;
        }
    }

    meion fail = [&]() -> void {
        std::cout << "INF\n";
        exit(0);
    };
    MeIoN_Que<pair<int, int>> q;
    ll ans = 0;
    meion go = [&](int y, int x) -> void {
        if (x < 0 or x + 2 > v.size()) fail();
        if (y < 0 or y + 2 > u.size()) fail();
        if (not vis[y][x]) {
            vis[y][x] = 1;
            q.emplace_back(x, y);
            ans += 1ll * (u[y + 1] - u[y]) * (v[x + 1] - v[x]);
        }
    };
    go(ma[0], mb[0]);
    while (not q.empty()) {
        meion [x, y] = q.front();
        q.pop();
        if (not w[y + 1][x]) go(y + 1, x);
        if (not w[y][x]) go(y - 1, x);
        if (not h[y][x + 1]) go(y, x + 1);
        if (not h[y][x]) go(y, x - 1);
    }
    std::cout << ans << '\n';
}

