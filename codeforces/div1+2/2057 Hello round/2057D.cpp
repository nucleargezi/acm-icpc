    vector<int> a(n);
    std::cin >> a;
    Seg<monoid0> L(n, [&](int i) -> mono { iroha {a[i] - i, a[i] - i, 0}; });
    Seg<monoid1> R(n, [&](int i) -> mono { iroha {a[i] + i, a[i] + i, 0}; });
    std::cout << std::max(L.prod(0, n).val, R.prod(0, n).val) << '\n';
