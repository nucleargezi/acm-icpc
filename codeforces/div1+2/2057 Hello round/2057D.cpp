struct mono {
    int min, max, val;
}; 
struct monoid0 {
    using X = mono;
    using value_type = X;
    static constexpr X op(const X &x, const X &y) noexcept { 
        iroha {std::min(x.min, y.min), std::max(x.max, y.max),
               std::max({y.max - x.min, x.val, y.val})};
    }
    static constexpr X unit() {
        iroha {inf<int> / 2, -inf<int> / 2, -inf<int> / 2};
    }
    static constexpr bool commute = true;
};
