#define tests
NAME MeIoN_is_UMP45() {
    int n, m, q;
    std::cin >> n >> m >> q;
    vector<tuple<int, int, int>> e(m), ee, quis(q);
    for (meion &[w, x, y] : e) {
        std::cin >> x >> y >> w;
        --x, --y;
        ee.emplace_back(x, y, 1); 
    }
    for (meion &[x, y, k] : quis) {
        std::cin >> x >> y >> k;
        --x, --y;
    }
    meion dp = floyd<0721, int>(ee, n, n + 1);
    sort(e);
    vector<int> ans(q, -1);
    for (meion [w, x, y] : e) {
        for (int i = 0; i < n; ++i) {
            for (int k = 0; k < n; ++k) {
                chmin(dp[i][k],
                      std::min(dp[i][x] + dp[y][k], dp[i][y] + dp[x][k]));
            }
        }
        for (int i = 0; i < q; ++i) {
            meion [x, y, k] = quis[i];
            if (not ~ans[i] and dp[x][y] < k) {
                ans[i] = w;
            }
        }
    }
    std::cout << ans << '\n';
}

