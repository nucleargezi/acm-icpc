using mint = modint<mod99>;
NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<int> a(n);
    std::cin >> a;
    vector<array<mint, 2>> dp(n + 2);
    dp[0] = {1, 0};
    mint ans;
    for (int i{}, l{}, r{}; i < n; ++i) {
        mint s = dp[a[i]][0] + dp[a[i]][1];
        int pla = (i ? a[i - 1] : 0);
        mint ss = (pla < n ? dp[pla][0] : 0);
        dp[l] = dp[r] = {0, 0};
        l = a[i], r = pla + 1;
        std::tie(dp[l][0], dp[r][1]) = {s, ss};
        ans = s + ss;
    }
    std::cout << ans << '\n';
}

