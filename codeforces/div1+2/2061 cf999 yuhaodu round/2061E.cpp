NAME MeIoN_is_UMP45() {
    ll n, m, k;
    std::cin >> n >> m >> k;
    vector<ll> a(n), b(m);
    std::cin >> a >> b;
    ll s = std::accumulate(a.begin(), a.end(), 0ll);
    vector<ll> v;
    vector<ll> bas(1 << m);
    for (int i{}; i < (1 << m); ++i) {
        ll s = (1 << 30) - 1;
        for (int k{}; k < m; ++k) {
            if (i >> k & 1) {
                s &= b[k];
            }
        }
        bas[i] = s;
    }
    for (ll x : a) {
        vector<ll> t(m + 1, inf<ll>);
        t[0] = x;
        for (int i{1}; i < (1 << m); ++i) {
            chmin(t[popcount(i)], x & bas[i]);
        }
        for (int i = 0; i < m; ++i) {
            v.emplace_back(t[i] - t[i + 1]);
        }
    }
    sort(v, greater());
    for (ll i{}; i < v.size() and i < k; ++i) {
        s -= v[i];
    }
    std::cout << s << '\n';
}

