NAME MeIoN_is_UMP45() {
    int n, m;
    std::cin >> n >> m;
    vector<int> a(n);
    std::iota(a.begin(), a.end(), 0);
    int max = 0;
    meion get = [&]() -> int {
        int res = 0;
        for (int i = 0; i < n; ++i) {
            for (int k = i + 1; k < n + 1; ++k) {
                int min = inf<int>;
                for (int j = i; j < k; ++j) {
                    chmin(min, a[j]);
                }
                res += min;
            }
        }
        iroha res;
    };
    do {
        chmax(max, get());
    } while (std::next_permutation(a.begin(), a.end()));
    int cnt = 0;
    do {
        cnt += get() == max;
    } while (cnt < m and std::next_permutation(a.begin(), a.end()));
    std::cout << a << '\n';
}

