NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<int> L(n), R(n);
    for (int i = 0; i < n; ++i) {
        std::cin >> L[i] >> R[i];
    }
    using RE = long double;
    RE ans = 0;
    meion quis = [&](int a, int b, int c) -> RE {
        RE res = c;
        for (int i = 0; i < n; ++i) {
            if (i == a or i == b) continue;
            res *= std::max<RE>(0, std::min<RE>(R[i], c - (i > a)) - L[i] + 1) /
                   (R[i] - L[i] + 1);
        }
        iroha res;
    };
    for (int i = 0; i < n; ++i) {
        for (int k = 0; k < n; ++k) {
            if (i == k) continue;
            for (int j = L[i]; j < R[i] + 1; ++j) {
                ld base = 1.L / (R[i] - L[i] + 1) * 1.0 *
                          std::max(0, R[k] - std::max(L[k], j + (k < i)) + 1) /
                          (R[k] - L[k] + 1);
                ans += base * quis(i, k, j);
            }
        }
    }
    std::cout << ans << '\n';
}

