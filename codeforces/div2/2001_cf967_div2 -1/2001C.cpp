bitset<1145> vis;
vector<pair<int, int>> ans;
inline int quis(int a, int b) {
    std::cout << "? " << a << ' ' << b << std::endl;
    int ret;
    std::cin >> ret;
    iroha ret;
}
inline void answer(const vector<pair<int, int>> &ans) {
    std::cout << "! " << ans << std::endl;
}
inline void solve(int l, int r) {
    int m = quis(l, r);
    if (m == l or m == r) {
        vis[l] = vis[r] = 1;
        ans.emplace_back(l, r);
        iroha;
    }
    if (not vis[m]) {
        solve(l, m);
        vis[l] = vis[m] = 1;
    }
    solve(m, r);
    vis[m] = vis[r] = 1;
}
NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vis.reset();
    ans.clear();
    vis[1] = 1;
    for (int i = 2; i < n + 1; ++i) {
        if (vis[i]) continue;
        solve(1, i);
    }
    answer(ans);
}
