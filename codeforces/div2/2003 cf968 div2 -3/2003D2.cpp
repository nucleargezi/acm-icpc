NAME MeIoN_is_UMP45() {
    int n, m;
    std::cin >> n >> m;
    int max = 0;
    vector<pair<int, int>> e;
    for (int i = 0, sz; i < n; ++i) {
        std::cin >> sz;
        set<int> se;
        for (int i = 0, x; i < sz; ++i) {
            std::cin >> x;
            se.emplace(x);
        }
        int x = 0, cnt = 0;
        int first, second;
        while (true) {
            if (not se.contains(x)) {
                ++cnt;
                if (cnt == 1) {
                    first = x;
                } else if (cnt == 2) {
                    second = x;
                    chmax(max, x);
                    break;
                }
            }
        ++x; }
        e.emplace_back(first, second);
    }

    vector<vector<int>> v(max + 1);
    vector<int> dp(max + 1);
    std::iota(dp.begin(), dp.end(), 0);
    for (const meion &[x, y] : e) {
        v[x].emplace_back(y);
    }
    for (int i = max + 1; i--; ) {
        for (const int to : v[i]) {
            chmax(dp[i], dp[to]);
        }
    }

    int maxx = -1;
    for (int i = 0; i < max + 1; ++i) {
        if (v[i].size() > 1) {
            chmax(maxx, dp[i]);
        }
        if (v[i].size() != 0) {
            chmax(maxx, i);
        }
    }

    ll ans = 0;

    for (int i = 0; i < m + 1; ++i) {
        if (i > max - 1) {
            ans += (1ll * max + m) * (m - max + 1) >> 1;
            break;
        } else {
            ans += std::max({i, dp[i], maxx});
        }
    }
    std::cout << ans << '\n';
}

