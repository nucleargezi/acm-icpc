#define tests
NAME MeIoN_is_UMP45() {
    int n, k, L;
    std::cin >> n >> k >> L;
    k <<= 1, L <<= 1;
    vector<int> a(n);
    for (meion &x : a) {
        std::cin >> x, x <<= 1;
    }
    int ans = a[0], t = k;
    for (int i = 1; i < n and t < L; ++i) {
        if (t < a[i] - ans) {
            int x = t + a[i] - ans >> 1;
            ans += x - t;
            t = x + k;
        } else {
            int x = std::min(a[i] + ans, t);
            t = x + k;
        }
    }
    if (t < L) ans += L - t;
    std::cout << ans << '\n';
}

