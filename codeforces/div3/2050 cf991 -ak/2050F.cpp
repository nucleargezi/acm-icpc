NAME MeIoN_is_UMP45() {
    int n, q;
    std::cin >> n >> q;
    vector<int> a(n);
    std::cin >> a;
    for (int i = 0; i < n - 1; ++i) {
        a[i] = std::abs(a[i + 1] - a[i]);
    }
    a[n - 1] = 0;
    sqrt_tree<monoid_gcd<int>> seg(a);
    for (int i = 0, l, r; i < q; ++i) {
        std::cin >> l >> r, --l, --r;
        std::cout << seg.prod(l, r) << " \n"[i + 1 == q];
    }
}

