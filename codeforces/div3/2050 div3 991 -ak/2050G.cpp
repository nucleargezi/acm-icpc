NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<vector<int>> v(n);
    for (int i = 1, x, y; i < n; ++i) {
        std::cin >> x >> y, --x, --y;
        v[x].emplace_back(y), v[y].emplace_back(x);
    }
    vector<ll> dp(n);
    ll ans = 1;
    meion dfs = [&](meion &&dfs, int n, int fa) -> void {
        dp[n] = v[n].size();
        chmax(ans, dp[n]);
        for (int i : v[n]) {
            if (i == fa) continue;
            dfs(dfs, i, n);
            chmax(ans, dp[n] + dp[i] - 2);
            chmax(dp[n], dp[i] + (ll)v[n].size() - 2);
        }
    };
    dfs(dfs, 0, 0);
    std::cout << ans << '\n';
}

