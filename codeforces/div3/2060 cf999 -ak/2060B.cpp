NAME MeIoN_is_UMP45() {
    int n, m;
    std::cin >> n >> m;
    vector a(n, vector<int>(m));
    std::cin >> a;
    for (meion &v : a) {
        sort(v);
    }
    vector<int> v(n);
    for (int i = 0; i < n; ++i) v[i] = a[i][0];
    meion rk = argsort(v);
    bool f = true;
    int pr = -1;
    for (int i = 0; i < m and f; ++i) {
        for (int k{}; k < n and f; ++k) {
            f &= a[rk[k]][i] > pr;
            pr = a[rk[k]][i];
        }
    }
    if (f) {
        for (meion &x : rk) {
            ++x;
        }
        std::cout << rk << '\n';
    } else {
        std::cout << "-1\n";
    }
}

