#include "MeIoN_Lib/MeIoN_all.hpp"
#include "MeIoN_Lib/math/mod/modint.hpp"

using mint = modint<mod99>;
constexpr int N = 114514;
mint dp[21][N];
void before() {
    std::cin.tie(nullptr)->sync_with_stdio(false);
    std::cout << std::fixed << std::setprecision(12);
    dp[0][1] = 1;
    for (int i{1}; i < 21; ++i) {
        for (int k{1}; k < N; ++k) {
            for (int j = k + k; j < N; j += k) {
                dp[i][j] += dp[i - 1][k];
            }
        }
    }
} 
#define tests
mint CC(mint x, mint y) {
    mint mul = x, c = y;
    mint res(1);
    for (; c.val; c -= 1, mul -= 1) {
        res *= mul;
        res *= c.inv();
    }
    iroha res;
}
NAME MeIoN_is_UMP45() {
    int K, n;
    std::cin >> K >> n;
    for (int i = 1; i < K + 1; ++i) {
        mint ans = 0;
        for (int k{}; k < std::min(n, 20) + 1; ++k) {
            ans += CC(n + 1, k + 1) * dp[k][i];
        }
        if (i == 1) ans = n;
        std::cout << ans << " \n"[i == K];
    }
}

