NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<int> a(n), b(n);
    std::cin >> a >> b;
    vector<tuple<int, int, int>> v;
    for (int i = 0; i < n; ++i) {
        v.emplace_back(std::min(a[i], b[i]), a[i], b[i]);
    }
    sort(v); 
    int s{};
    a.clear(), b.clear();
    for (int t{}; meion [_, x, y] : v) {
        if (x > y) {
            ++s;
            std::swap(x, y);
        }
        a.emplace_back(x);
        b.emplace_back(y);
    }
    int ok{1};
    for (int i{1}; i < n and ok; ++i) {
        ok &= b[i] > b[i - 1];
    }
    int ss{};
    for (int i{1}; i < n; i += 2) {
        ss += a[i] > b[i - 1] and b[i] > a[i - 1];
    }
    for (int i{n - 1}; i > 0; i -= 2) {
        ss += a[i] > b[i - 1] and b[i] > a[i - 1];
    }
    Yes(ok and (~s & 1 or ss or n & 1));
}

