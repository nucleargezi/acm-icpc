NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<vector<int>> v(n);
    for (int i = 0, x; i < n; ++i) {
        std::cin >> x, --x;
        v[i].emplace_back(x);
    }

    vector<int> dfn(n), low(n), s, sz(n), id(n);
    int tot = 0, cnt = 0;
    vector<uint8_t> vis(n);
    meion tarjan = [&](meion &&tarjan, int n) -> void {
        dfn[n] = low[n] = ++tot;
        s.emplace_back(n);
        vis[n] = 1;
        for (int i : v[n]) {
            if (not dfn[i]) {
                tarjan(tarjan, i);
                chmin(low[n], low[i]);
            } else if (vis[i]) {
                chmin(low[n], dfn[i]);
            }
        }
        if (dfn[n] == low[n]) {
            while (true) {
                int i = s.back();
                s.pop_back();
                id[i] = cnt;
                vis[i] = 0;
                ++sz[cnt];
                if (i == n) break;
            }
            ++cnt;
        }
    };
    for (int i = 0; i < n; ++i) {
        if (not dfn[i]) tarjan(tarjan, i);
    }
    
    vector<int> in(cnt);
    vector<vector<int>> g(cnt);
    for (int i = 0; i < n; ++i) {
        for (int x : v[i]) {
            if (id[i] == id[x]) continue;
            g[id[i]].emplace_back(id[x]);
            ++in[id[x]];
        }
    }
    MeIoN_Que<pair<int, int>> q;
    for (int i = 0; i < cnt; ++i) {
        if (not in[i]) q.emplace_back(i, 1);
    }
    
    int ans = 0;
    while (not q.empty()) {
        meion [n, d] = q.front();
        q.pop();
        chmax(ans, d);
        for (int i : g[n]) {
            if (not --in[i]) {
                q.emplace_back(i, d + 1);
            }
        }
    }

    std::cout << ans + 1 << '\n';
}

