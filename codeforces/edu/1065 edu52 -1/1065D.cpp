NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    static int a[11][11];
    for (int i = 0; i < n; ++i) {
        for (int k = 0; k < n; ++k) {
            std::cin >> a[i][k], --a[i][k];
        }
    }
    static pair<ll, ll> dis[3][11][11][121];
    for (int i = 0; i < (3 * 121 * 121); ++i) {
        dis[0][0][0][i] = {inf<ll>, inf<ll>};
    }
    using node = std::tuple<pair<ll, ll>, int, int, int, int>;
    priority_queue<node, vector<node>, greater<>> q;
    meion add = [&](int type, int x, int y, int n, pair<ll, ll> dist) -> void {
        if (chmin(dis[type][x][y][n], dist)) q.emplace(dist, type, x, y, n);
    };
    for (int i = 0; i < n; ++i) {
        for (int k = 0; k < n; ++k) {
            if (a[i][k] == 0) {
                for (int type = 0; type < 3; ++type) {
                    add(type, i, k, 0, {0, 0});
                }
            }
        }
    }
    while (not q.empty()) {
        const meion [dist, type, x, y, val] = q.top();
        q.pop();
        if (dis[type][x][y][val] != dist) continue;
        if (a[x][y] == val) add(type, x, y, val + 1, dist);
        for (int new_type = 0; new_type < 3; ++new_type) {
            add(new_type, x, y, val, {dist.first + 1, dist.second + 1});
        }
        for (int i = 0; i < n; ++i) {
            for (int k = 0; k < n; ++k) {
                int dx = std::abs(x - i), dy = std::abs(y - k);
                int ok = 0;
                if (type == 0 and dx + dy == 3 and dx * dy == 2) ok = 1;
                if (type == 1 and dx == 0) ok = 1;
                if (type == 1 and dy == 0) ok = 1;
                if (type == 2 and dx == dy) ok = 1;
                if (ok) add(type, i, k, val, {dist.first + 1, dist.second});
            }
        }
    }
    pair<ll, ll> ans{inf<ll>, inf<ll>};
    for (int i = 0; i < n; ++i) {
        for (int k = 0; k < n; ++k) {
            if (a[i][k] == n * n - 1) {
                for (int type = 0; type < 3; ++type) {
                    chmin(ans, dis[type][i][k][n * n]);
                }
            }
        }
    }
    std::cout << ans << '\n';
}

