using mint = modint<mod99>;
NAME MeIoN_is_UMP45() {
    int n, m, A;
    std::cin >> n >> m >> A;
    mint ans = 1, inv2 = inv<mint>(2);
    int pr = 0;
    for (int i = 0, b; i < m; ++i) {
        std::cin >> b;
        mint tp = mint(A).ksm(b - pr);
        ans *= tp * (tp + 1) * inv2;
        pr = b;
    }
    ans *= mint(A).ksm(n - pr * 2);
    std::cout << ans << '\n';
}

