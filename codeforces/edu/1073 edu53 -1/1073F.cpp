NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<vector<int>> v(n);
    for (int i = 1, x, y; i < n; ++i) {
        std::cin >> x >> y, --x, --y;
        v[x].emplace_back(y), v[y].emplace_back(x);
    }

    vector<int> dis(n);
    vector<int> yua(n, -1);
    int max = -1, AL = 0;
    int S_1, S_2, E_1(-1), E_2(-1);
    meion dfs = [&](meion &&dfs, int n, int fa) -> void {
        int son = 0, se = -1;
        for (int i : v[n]) {
            if (i == fa) continue;
            dis[i] = dis[n] + 1;
            dfs(dfs, i, n);
            if (yua[n] == -1 or dis[yua[i]] > dis[yua[n]]) {
                se = yua[n], yua[n] = yua[i];
            } else if (se == -1 or dis[yua[i]] > dis[se]) {
                se = yua[i];
            }
            ++son;
        }
        if (son == 0) yua[n] = n;
        if (son > 1 and (max == -1 or
                         (dis[n] > dis[max] or
                          dis[n] == dis[max] and dis[yua[n]] + dis[se] > AL))) {
            max = n;
            AL = dis[yua[n]] + dis[se];
        }
    };
    meion ser = [&](meion &&ser, int n) -> void {
        int son = 0;
        for (int i : v[n]) {
            if (dis[i] > dis[n]) {
                ++son;
                ser(ser, i);
            }
        }
        if (son == 0) {
            if (E_1 == -1 or dis[E_1] < dis[n]) {
                E_2 = E_1;
                E_1 = n;
            } else if (E_2 == -1 or dis[E_2] < dis[n]) {
                E_2 = n;
            }
        }
    };
    dfs(dfs, 0, 0);
    ser(ser, max);
    S_1 = E_1, S_2 = E_2;
    fill(dis, 0);
    fill(yua, -1);
    max = -1, AL = 0;
    dfs(dfs, max, max);
    E_1 = E_2 = -1;
    ser(ser, max);
    std::cout << pair(S_1 + 1, E_1 + 1) << '\n' << pair(S_2 + 1, E_2 + 1) << '\n';
}

