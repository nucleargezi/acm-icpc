NAME MeIoN_is_UMP45() {
    ll n;
    std::cin >> n;
    ld l = 0, r = n;
    for (int i = 0; i < 100; ++i) {
        ld a = l / 2 + r / 2;
        ld b = n - a;
        if (a + b > a * b) {
            l = a;
        } else {
            r = a;
        }
    }
    ld a = l, b = n - a;
    if (std::abs((a + b) - (a * b)) <= 1e-6L) {
        std::cout << "Y " << pair(a, b) << '\n';
    } else {
        std::cout << "N\n";
    }
}

