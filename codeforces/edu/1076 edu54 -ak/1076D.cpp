NAME MeIoN_is_UMP45() {
    int n, m, k;
    std::cin >> n >> m >> k;
    vector<vector<pair<int, int>>> v(n);
    vector<vector<int>> e(n);
    for (int i = 0, x, y, w; i < m; ++i) {
        std::cin >> x >> y >> w;
        --x, --y;
        v[x].emplace_back(y, w);
        v[y].emplace_back(x, w);
        e[x].emplace_back(i);
        e[y].emplace_back(i);
    }
    meion [dis, fa] = dijkstra<ll>(v, 0);
    vector<int> ans, s{0};
    while (not s.empty()) {
        meion n = s.back();
        s.pop_back();
        for (int i = 0; i < v[n].size(); ++i) {
            if (fa[v[n][i].first] == n) {
                ans.emplace_back(e[n][i] + 1);
                s.emplace_back(v[n][i].first);
            }
        }
    }
    if (ans.size() > k) ans.resize(k);
    std::cout << ans.size() << '\n' << ans << '\n';
}

