NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<vector<int>> v(n);
    for (int i = 1, x, y; i < n; ++i) {
        std::cin >> x >> y;
        --x, --y;
        v[x].emplace_back(y);
        v[y].emplace_back(x);
    }
    int m;
    std::cin >> m;
    vector<vector<pair<int, int>>> todo(n);
    for (int i = 0, x, d, w; i < m; ++i) {
        std::cin >> x >> d >> w;
        --x;
        todo[x].emplace_back(d, w);
    }
    vector<ll> ans(n), val(n + 1);
    const int sz = n;
    meion ser = [&](meion &&ser, int n, int fa, int dis) -> void {
        for (meion [d, w] : todo[n]) {
            val[dis] += w;
            val[std::min(dis + d + 1, sz)] -= w;
        }
        ans[n] = val[dis];
        for (int i : v[n]) {
            if (i == fa) continue;
            val[dis + 1] += val[dis];
            ser(ser, i, n, dis + 1);
            val[dis + 1] -= val[dis];
        }
        for (meion [d, w] : todo[n]) {
            val[dis] -= w;
            val[std::min(dis + d + 1, sz)] += w;
        }
    };
    ser(ser, 0, 0, 0);
    std::cout << ans << '\n';
}

