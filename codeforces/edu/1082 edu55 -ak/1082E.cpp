NAME MeIoN_is_UMP45() {
    int n, c;
    std::cin >> n >> c;
    vector<int> a(n);
    std::cin >> a;
    int len = std::max(qmax(a), c) + 1;
    vector<vector<int>> v(len);
    for (int i = 0; i < n; ++i) {
        if (a[i] != c) {
            v[a[i]].emplace_back(i);
            a[i] = 0;
        } else {
            a[i] = 1;
        }
    }
    a = pre_sum(a);

    int ans = a.back();
    for (meion &vec : v) {
        for (int pr = 0, cnt = 0; int i : vec) {
            chmax(pr, a[i] - cnt++);
            chmax(ans, cnt - a[i] + pr + a.back());
        }
    }
    std::cout << ans << '\n';
}

