using mint = modint<mod99>;
NAME MeIoN_is_UMP45() {
    int n, s, r;
    std::cin >> n >> s >> r;
    mint tot = C<mint>(s - r + n - 1, s - r);
    mint ans;
    for (int i = r; i < s + 1; ++i) {
        for (int k = 1; k < n + 1 and k * i < s + 1; ++k) {
            mint t;
            int d = n - k;
            for (int j = 0; (j + k) * i < s + 1 and j + k < n + 1; ++j) {
                int x = s - (k + j) * i;
                t += C<mint>(n - k, j) * ((j & 1) ? -1 : 1) *
                     (d == 0 ? x == 0 : C<mint>(x + d - 1, d - 1));
            }
            t *= C<mint>(n - 1, k - 1);
            ans += t * mint(k).inv();
        }
    }
    std::cout << ans / tot << '\n';
}

