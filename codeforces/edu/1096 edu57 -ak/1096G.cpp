using mint = modint<mod99>;
NAME MeIoN_is_UMP45() {
    int n, k;
    std::cin >> n >> k;
    vector<mint> f(n / 2 * 10 + 1);
    for (int i = 0, x; i < k; ++i){
        std::cin >> x;
        f[x] = 1;
    }
    f = fps_pow(f, n >> 1);
    mint ans;
    for (meion &x : f) {
        ans += x * x;
    }
    std::cout << ans << '\n';
}

