NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<array<int, 3>> a(n);
    for (int i = 0; meion &[l, r, id] : a) {
        std::cin >> l >> r, id = i++;
    }
    sort(a);
    vector<int> ans(n);
    for (int i = 1; i < n; ++i) {
        ans[a[i][2]] = ans[a[i - 1][2]] ^ (a[i][0] > a[i - 1][1]);
        chmax(a[i][1], a[i - 1][1]);
    }
    if (qmax(ans) == 0) {
        std::cout << "-1\n";
    } else {
        for (meion &x : ans) {
            ++x;
        }
        std::cout << ans << '\n';
    }
}

