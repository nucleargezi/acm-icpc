NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<int> a(n);
    std::cin >> a;
    sieve(qmax(a));
    vector<vector<int>> v(n);
    for (int i = 1, x, y; i < n; ++i) {
        std::cin >> x >> y, --x, --y;
        v[x].emplace_back(y);
        v[y].emplace_back(x);
    }
    int ans = qmax(a) > 1;
    vector<int> dis(n);
    vector<map<int, int>> set(n);
    meion dfs = [&](meion &&dfs, int n, int fa) -> void {
        [&]() -> void {
            int x = a[n];
            while (minp[x]) {
                set[n][minp[x]] = dis[n];
                x /= minp[x];
            }
        }();
        for (int i : v[n]) {
            if (i == fa) continue;
            dis[i] = dis[n] + 1;
            dfs(dfs, i, n);
        }
        for (meion &[p, ndis] : set[n]) {
            vector<int> son;
            for (int i : v[n]) {
                if (i == fa) continue;
                if (set[i].contains(p)) {
                    int idis = set[i][p];
                    son.emplace_back(idis);
                    chmax(ans, idis - dis[n] + 1);
                    chmax(ndis, idis);
                }
            }
            if (son.size() > 1) {
                std::partial_sort(son.begin(), son.begin() + 2, son.end(),
                                  greater());
                chmax(ans, son[0] + son[1] - dis[n] - dis[n] + 1);
            }
        }
    };
    dfs(dfs, 0, 0);
    std::cout << ans << '\n';
}

