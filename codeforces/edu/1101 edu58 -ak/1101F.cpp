NAME MeIoN_is_UMP45() {
    int n, m;
    std::cin >> n >> m;
    vector<int> a(n);
    std::cin >> a;

    static int dp[0721 - 63][0721 - 63][0721 - 63];
    for (int i = 0; i < n; ++i) {
        dp[i][0][i] = 0;
        for (int k = i + 1; k < n; ++k) {
            dp[i][0][k] = inf<int>;
        }
        for (int k = 1; k < n - i + 1; ++k) {
            dp[i][k][i] = 0;
            for (int j = i + 1, l = i; j < n; ++j) {
                dp[i][k][j] = dp[i][k - 1][j];
                while (l + 1 < j and a[j] - a[l + 1] > dp[i][k - 1][l + 1]) {
                    ++l;
                }
                dp[i][k][j] = std::min(a[j] - a[l], dp[i][k - 1][l + 1]);
            }
        }
    }
    ll ans = 0;
    vector<std::tuple<int, int, int, int>> car(m);
    for (meion &[s, t, c, r] : car) {
        std::cin >> s >> t >> c >> r, --s, --t;
        int x = std::min(r + 1, t - s);
        chmax(ans, 1ll * dp[s][x][t] * c);
    }
    std::cout << ans << '\n';
}

