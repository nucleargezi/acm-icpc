struct LinearBasis {
    static const int B = 30;
    int cnt;
    LinearBasis() : cnt(0) { memset(basis, -1, sizeof(basis)); }
    void add(int v) {
        v = ask(v);
        if (v) {
            ++cnt;
            int pivot = 30 - clz(v);
            for (int i = 0; i < B; ++i) {
                if (~basis[i] && (basis[i] >> pivot & 1)) {
                    basis[i] ^= v;
                }
            }
            basis[pivot] = v;
        }
    }
    int ask(int v) const {
        for (int i = B; i--;) {
            if ((v >> i & 1) and ~basis[i]) {
                v ^= basis[i];
            }
        }
        return v;
    }
    int basis[B];
};
NAME MeIoN_is_UMP45() {
    static LinearBasis g;
    int n, s = 0;
    std::cin >> n;
    for (int i = n, x; i--; ) {
        std::cin >> x;
        s ^= x;
        g.add(s);
    }
    std::cout << (s ? g.cnt : -1) << '\n';
}

