NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    int s = 0, t = n << 1 | 1;
    mcf_graph<int, ll, true> FL(n + 1 << 1);
    for (int i = n; i--; ) {
        FL.add(s, i + 1, 1, 0);
    }
    for (ll i = 0, a, b, day; i < n; ++i) {
        std::cin >> a >> b >> day;
        for (ll k = 0; k < n; ++k) {
            ll cost = a - b * std::min(day, k);
            if (cost < 1) continue;
            FL.add(i + 1, k + n + 1, 1, -cost);
        }
    }
    for (int i = n; i--; ) {
        FL.add(i + n + 1, t, 1, 0);
    }
    ll ans = 0;
    for (meion [x, y] : FL.slope(s, t)) {
        chmax(ans, -y);
    }
    std::cout << ans << '\n';
}

