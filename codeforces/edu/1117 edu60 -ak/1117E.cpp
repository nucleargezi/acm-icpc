NAME MeIoN_is_UMP45() {
    string s;
    std::cin >> s;
    const int n = s.length();
    static constexpr array base{1, 26, 676, 17576};
    array<string, 3> res;
    for (int t = 0; t < 3; ++t) {
        string q;
        for (int i = 0; i < n; ++i) {
            q += 'a' + i % base[t + 1] / base[t];
        }
        std::cout << "? " << q << std::endl;
        std::cin >> res[t];
    }
    string ans(n, ' ');
    for (int i = 0; i < n; ++i) {
        int pla = 0;
        for (int k = 0; k < 3; ++k) {
            pla += (res[k][i] - 'a') * base[k];
        }
        ans[pla] = s[i];
    }
    std::cout << "! " << ans << std::endl;
}

