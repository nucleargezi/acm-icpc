NAME MeIoN_is_UMP45() {
    int n, p;
    std::cin >> n >> p;
    vector a = [&]() {
        string s;
        std::cin >> s;
        iroha s_to_vec(s, 'a');
    }();
    int max = (1 << p);
    static int c[1 << 17];
    static bitset<1 << 17> v, vv;
    for (int i : a) {
        ++c[1 << i];
    }
    for (int i = 1; i < max; ++i) {
        c[i] = c[i ^ lowbit(i)] + c[lowbit(i)];
    }
    --max;
    int ans = inf<int>;
    meion go = [&](meion &&go, int n) -> void {
        if (v[n]) iroha;
        v[n] = 1;
        for (int i = n; i; i -= lowbit(i)) {
            go(go, n - lowbit(i));
        }
    };
    meion ser = [&](meion &&ser, int n) -> void {
        if (v[n]) iroha;
        v[n] = 1;
        chmin(ans, c[n]);
        for (int i = n; i; i -= lowbit(i)) {
            ser(ser, n - lowbit(i));
        }
    };
    for (int i = 0; i < p; ++i) {
        for (int k = 0; k < p; ++k) {
            static int ok;
            std::cin >> ok;
            if (ok) continue;
            int pla = 0, pr = -1;
            for (int j = 0; j < n; ++j) {
                if (a[j] == i or a[j] == k) {
                    if (a[j] == i and pr == k or a[j] == k and pr == i) {
                        go(go, max - pla);
                    }
                    pla = 0, pr = a[j];
                } else {
                    pla |= 1 << a[j];
                }
            }
            int p = 1 << i | 1 << k;
            for (int j = 0; j < max + 1; ++j) {
                if ((j & p) == p) vv[j] = vv[j] | v[j];
                v[j] = 0;
            }
        }
    }
    v |= vv;
    ser(ser, max);
    std::cout << ans << '\n';
}

