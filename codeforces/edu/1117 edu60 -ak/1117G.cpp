NAME MeIoN_is_UMP45() {
    int n, q;
    std::cin >> n >> q;
    vector<int> a(n), l(q), r(q);
    std::cin >> a;
    vector<vector<int>> al(q), ar(q);
    for (int i = 0; i < q; ++i) {
        std::cin >> l[i], --l[i];
        al[l[i]].emplace_back(i);
    }
    for (int i = 0; i < q; ++i) {
        std::cin >> r[i], --r[i];
        ar[r[i]].emplace_back(i);
    }
    vector<int> el(n, -1), er(n, n);
    vector<int> s;
    for (int i = n; i--; ) {
        while (s.size() and a[i] > a[s.back()]) {
            el[s.back()] = i;
            s.pop_back();
        }
        s.emplace_back(i);
    }
    s.clear();
    for (int i = 0; i < n; ++i) {
        while (s.size() and a[i] > a[s.back()]) {
            er[s.back()] = i;
            s.pop_back();
        }
        s.emplace_back(i);
    }
    lazy_seg<a_monoid_sum_add<ll>> seg(n);
    vector<ll> ans(q);
    for (int i = 0; i < n; ++i) {
        if (el[i] < i - 1) {
            seg.apply(el[i] + 1, i, 1);
        }
        for (int x : ar[i]) {
            ans[x] = seg.prod(l[x], i + 1);
        }
    }
    seg.build(n);
    for (int i = n; i--; ) {
        seg.apply(i, er[i], 1);
        for (int x : al[i]) {
            ans[x] += seg.prod(i, r[x] + 1);
        }
    }
    std::cout << ans << '\n';
}

