NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<int> a(n);
    std::cin >> a;
    a = pre_sum(a);
    int ans = 0;
    for (int i = 0; i < n; ++i) {
        for (int k = 0; k < i; ++k) {
            ans += i - k;
        }
    }
    for (int l = 0; l < n; ++l) {
        for (int r = l + 1; r < n + 1; ++r) {
            int s = a[l] + a[r];
            if (s & 1) continue;
            if (std::binary_search(a.begin(), a.end(), s >> 1)) --ans;
        }
    }
    hash_map<int> ma;
    for (int i = 0; i < n + 1; ++i) {
        for (int k = i + 1; k < n + 1; ++k) {
            int s = a[i] + a[k];
            ans -= ma[s] << 1;
            ++ma[s];
        }
    }
    std::cout << ans << '\n';
}
