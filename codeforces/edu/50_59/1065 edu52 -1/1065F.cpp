NAME MeIoN_is_UMP45() {
    int n, k;
    std::cin >> n >> k;
    vector<vector<int>> v(n);
    vector<int> out(n);
    for (int i = 1, fa; i < n; ++i) {
        std::cin >> fa, --fa;
        v[fa].emplace_back(i);
        ++out[fa];
    }
    [&]() {
        LCA<22> lca(v, 0);
        for (int i = 0; i < n; ++i) {
            if (not out[i]) {
                int fa = lca.jump(i, k - 1);
                if (fa != i) v[i].emplace_back(fa);
            }
        }
    }();
    vector<int> dfn(n), low(n), s, sz(n), id(n);
    int tot = 0, cnt = 0;
    vector<uint8_t> vis(n);
    meion tarjan = [&](meion &&se, int n) -> void {
        low[n] = dfn[n] = ++tot;
        vis[n] = 1;
        s.emplace_back(n);
        for (const int i : v[n]) {
            if (not dfn[i]) {
                se(se, i);
                chmin(low[n], low[i]);
            } else if (vis[i]) {
                chmin(low[n], dfn[i]);
            }
        }
        if (low[n] == dfn[n]) {
            while (true) {
                int i = s.back();
                s.pop_back();
                vis[i] = 0;
                id[i] = cnt, sz[cnt] += not out[i];
                if (i == n) break;
            }
            ++cnt;
        }
    };
    tarjan(tarjan, 0);
    vector<vector<int>> g(cnt);
    for (int i = 0; i < n; ++i) {
        for (const int x : v[i]) {
            if (id[x] != id[i]) {
                g[id[i]].emplace_back(id[x]);
            }
        }
    }
    meion dfs = [&](meion &&se, int n) -> void {
        for (const int i : g[n]) {
            sz[i] += sz[n];
            se(se, i);
        }
    };
    dfs(dfs, id[0]);
    std::cout << qmax(sz) << '\n';
}

