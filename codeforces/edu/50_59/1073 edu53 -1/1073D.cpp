NAME MeIoN_is_UMP45() {
    int n;
    ll s;
    std::cin >> n >> s;
    vector<ll> a(n);
    std::cin >> a;
    ll ans = 0;
    while (true) {
        ll sum = 0, cnt = 0;
        for (const ll x : a) {
            if (x < s + 1) {
                s -= x;
                sum += x;
                ++cnt;
            }
        }
        if (sum == 0) break;
        ans += s / sum * cnt + cnt;
        s %= sum;
    }
    std::cout << ans << '\n';
}

