NAME MeIoN_is_UMP45() {
    int n;
    string s;
    std::cin >> n >> s;
    vector v = s_to_vec(s, 'S');
    for_each(v, [](int &x) { x = not not x; });
    meion paste = [&](const vector<int> &v) {
        vector<int> ret(v), cnt(v.size() + 1);
        ret.insert(ret.begin(), 0);
        for (int i = 0; i < n; ++i) {
            if (ret[i + 1]) {
                ret[i + 1] += ret[i];
                cnt[i + 1] = not ret[i];
            }
            cnt[i + 1] += cnt[i];
        }
        iroha pair{ret, cnt};
    };
    int ans = 0;
    for (int i = 2; i--; rev(v)) {
        meion [c, cnt] = paste(v);
        for (int i = n; i--; ) {
            if (c[i]) chmax(c[i], c[i + 1]);
        }
        chmax(ans, qmax(c) + (cnt.back() > 1));
        for (int i = 1; i < n; ++i) {
            if (not c[i] and c[i - 1]) {
                if (c[i + 1]) {
                    chmax(ans, c[i - 1] + 1);
                }
                if (c[i + 1]) {
                    chmax(ans, c[i - 1] + c[i + 1]);
                    if (cnt[i + 1] != cnt.back()) {
                        chmax(ans, c[i - 1] + c[i + 1] + 1);
                    }
                }
            }
        }
    };
    std::cout << ans << '\n';
}

