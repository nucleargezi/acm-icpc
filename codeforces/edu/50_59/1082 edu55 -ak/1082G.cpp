NAME MeIoN_is_UMP45() {
    int n, m;
    std::cin >> n >> m;
    int s = n + m + 1, t = s + 1;
    FL::III(s, t, t + 1);
    for (int i = 0, x; i < n; ++i) {
        std::cin >> x;
        FL::add(s, i, x);
    }
    ll ans = 0;
    for (int i = 0, x, y, w; i < m; ++i) {
        std::cin >> x >> y >> w;
        --x, --y;
        FL::add(x, n + i, inf<ll>);
        FL::add(y, n + i, inf<ll>);
        FL::add(n + i, t, w);
        ans += w;
    }
    std::cout << ans - FL::sap() << '\n';
}

