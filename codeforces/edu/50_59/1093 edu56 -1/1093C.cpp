NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<ll> b(n >> 1);
    std::cin >> b;
    vector<ll> a, ra;
    for (ll l = 0, r = inf<ll>; ll x : b) {
        ll nl = l, nr = x - l;
        if (nr > r) nl += nr - r, nr = r;
        a.emplace_back(nl);
        ra.emplace_back(nr);
        std::tie(l, r) = {nl, nr};
    }
    rev(ra);
    a.insert(a.end(), ra.begin(), ra.end());
    std::cout << a << '\n';
}

