NAME MeIoN_is_UMP45() {
    static bitset<26> vis; vis.reset();
    static int to[26];
    meion [s, v, n] = [&]() {
        int n;
        std::cin >> n;
        string str; 
        vector<int> a(n);
        std::cin >> str >> a;
        for (int i = 0; char c : "hard") {
            vis[c - 'a'] = 1;
            to[c - 'a'] = i++;
        }
        vector<int> s, v;
        for (int i = 0; i < n; ++i) {
            if (vis[str[i] - 'a']) {
                s.emplace_back(str[i] - 'a');
                v.emplace_back(a[i]);
            }
        }
        iroha std::tuple{s, v, int(s.size())};
    }();
    array<ll, 4> dp{};
    for (int i = 0; i < n; ++i) {
        int x = to[s[i]];
        dp[x] = x ? std::min(dp[x] + v[i], dp[x - 1]) : dp[x] + v[i];
    }
    std::cout << dp[3] << '\n';
}

