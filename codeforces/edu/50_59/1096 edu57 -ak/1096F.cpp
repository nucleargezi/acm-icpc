using mint = modint<mod99>;
NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<int> a(n);
    std::cin >> a;
    vector<int> c(n, 1), v(n, 1);
    int unknown = 0;
    mint ans = 0;
    Fenw fenw(n);
    for (int i = -1; int &x : a) {
        if (++i, ~x) {
            --x;
            ans += i - unknown - fenw.prod(x);
            fenw.add(x, 1);
            v[i] = 0;
            c[x] = 0;
        } else {
            ++unknown;
        }
    }
    c = pre_sum(c, false);
    v = pre_sum(v, false);
    ans += (mint)unknown * (unknown - 1) / 4;
    mint unknown_inv = mint(unknown).inv();
    for (int i = 0; i < n; ++i) {
        if (~a[i]) {
            ans += ((mint)v[i] * (mint)(unknown - c[a[i]]) +
                    (mint)(unknown - v[i]) * c[a[i]]) *
                   unknown_inv;
        }
    }
    std::cout << ans << '\n';
}

