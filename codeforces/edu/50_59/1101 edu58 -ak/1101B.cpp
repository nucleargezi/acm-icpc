NAME MeIoN_is_UMP45() {
    string s;
    std::cin >> s;
    const int n = s.length();
    int l, r = 0, ans = -1;
    vector<int> c(n + 1);
    for (int i = 0, x = 0; i < n; ++i) {
        c[i + 1] += c[i];
        if (s[i] == '|') c[i + 1]++;
        if (x == 0 and s[i] == '[') ++x;
        if (x == 1 and s[i] == ':') ++x, l = i;
        else if (x > 1 and s[i] == ':') chmax(r, i), ++x;
        if (x > 2 and s[i] == ']') chmax(ans, c[r + 1] - c[l] + 4);
    }
    std::cout << (ans < 4 ? -1 : ans) << '\n';
}

