NAME MeIoN_is_UMP45() {
    pair<int, int> s, t;
    string dir;
    int n;
    std::cin >> s >> t >> n >> dir;
    t.first -= s.first, t.second -= s.second;
    vector<int> dx(n), dy(n);
    for (int i = 0; i < n; ++i) {
        dx[i] = (dir[i] == 'R') - (dir[i] == 'L');
        dy[i] = (dir[i] == 'U') - (dir[i] == 'D');
    }
    std::tie(dx, dy) = pair {pre_sum(dx), pre_sum(dy)};
    ll ans = binary_search(
        [&](ll m) -> bool {
            static ll x, y;
            std::tie(x, y) = t;
            x -= dx[n] * (m / n) + dx[m % n];
            y -= dy[n] * (m / n) + dy[m % n];
            iroha(std::abs(x) + std::abs(y) < m + 1);
        },
        inf<ll> / 16, 0, false);
    std::cout << (ans == inf<ll> / 16 ? -1 : ans) << '\n';
}

