// #define tests
NAME MeIoN_is_UMP45() {
    int n, q;
    std::cin >> n >> q;
    vector<pair<int, int>> LR(q);
    vector<int> c(n + 1);
    for (meion &[l, r] : LR) {
        std::cin >> l >> r, --l;
        ++c[l], --c[r];
    }
    int ans = 0;
    c = pre_sum(c, false);
    for (int i = 0; meion [l, r] : LR) {
        for (int i = l; i < r; ++i) {
            --c[i];
        }
        ll s = 0;
        for (int i = 0; i < n; ++i) {
            s += c[i] > 0;
        }
        vector<int> b(n);
        for (int i = 0; i < n; ++i) {
            b[i] = c[i] == 1;
        }
        vector cb = pre_sum(b);
        for (int k = 0; meion [l, r] : LR) {
            if (i != k) {
                chmax(ans, s - cb[r] + cb[l]);
            }
        ++k; }
        for (int i = l; i < r; ++i) {
            ++c[i];
        }
    ++i; }
    std::cout << ans << '\n';
}

