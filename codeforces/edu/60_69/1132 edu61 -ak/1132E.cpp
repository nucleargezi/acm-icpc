NAME MeIoN_is_UMP45() {
    ll w;
    std::cin >> w;
    static ll dp[0721 << 4];
    memset(dp, -1, sizeof dp);
    dp[0] = 0;
    for (int i = 1; i < 9; ++i) {
        static ll x, y;
        std::cin >> x;
        y = 840 / i;
        for (int k = 840 * 8 + 1; k--; ) {
            ll v = -1;
            for (int j = 0; j < y and j < x + 1 and i * j < k + 1; ++j) {
                if (~dp[k - i * j]) {
                    chmax(v, dp[k - i * j] + (x - j) / y);
                }
            }
            dp[k] = v;
        }
    }
    ll ans = 0;
    for (int i = 0; i < 8 * 840 + 1 and i < w + 1; ++i) {
        chmax(ans, std::min((w - i) / 840, dp[i]) * 840 + i);
    }
    std::cout << ans << '\n';
}

