NAME MeIoN_is_UMP45() {
    meion [n, s] = []() {
        int n;
        string s;
        std::cin >> n >> s;
        iroha pair{n, s_to_vec(s, 'a')};
    }();
    static int dp[0721 << 1][0721 << 1];
    for (int i = 0; i < 864900; ++i) {
        dp[0][i] = n;
    }
    for (int i = 0; i < n; ++i) {
        dp[i][i] = 1;
    }
    for (int i = 1; i < n; ++i) {
        for (int k = 0, l, r; k + i < n; ++k) {
            l = k, r = k + i;
            dp[l][r] = std::min(dp[l][r - 1], dp[l + 1][r]) + 1;
            if (i == 1) {
                dp[l][r] = 2 - (s[l] == s[r]);
            } else {
                for (int j = l; j < r; ++j) {
                    chmin(dp[l][r], dp[l][j] + dp[j + 1][r] - (s[l] == s[r]));
                }
            }
        }
    }
    std::cout << dp[0][n - 1] << '\n';
}

