using mint = modint<mod99>;
NAME MeIoN_is_UMP45() {
    int n;
    mint m;
    std::cin >> n >> m;
    vector<int> v[2];
    for (int i = 0, x; i < n; ++i) {
        std::cin >> x;
        v[i & 1].emplace_back(x);
    }
    meion sol = [&](const vector<int> &v) -> mint {
        mint ret = 1;
        const int n = v.size();
        int lst = -1;
        for (int i = 0; i < n; ++i) {
            if (~v[i]) {
                if (~lst) {
                    array<mint, 3> dp{1, 0, 0};
                    for (int k = lst; k++ < i; ) {
                        dp = {dp[1] + dp[2], dp[0] + dp[2],
                              (dp[0] + dp[1]) * (m - 2) + dp[2] * (m - 3)};
                    }
                    ret *= dp[v[lst] != v[i]];
                } else {
                    ret *= (m - 1).ksm(i);
                }
                lst = i;
            }
        }
        if (~lst) {
            ret *= (m - 1).ksm(n - 1 - lst);
        } else {
            ret *= m * (m - 1).ksm(n - 1);
        }
        iroha ret;
    };
    std::cout << sol(v[0]) * sol(v[1]) << '\n';
}

