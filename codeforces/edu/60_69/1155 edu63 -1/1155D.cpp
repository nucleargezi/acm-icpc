NAME MeIoN_is_UMP45() {
    int n, x;
    std::cin >> n >> x;
    vector<int> a(n);
    std::cin >> a;
    vector<array<ll, 3>> dp(n);
    chmax(dp[0][0], a[0]), chmax(dp[0][1], 1ll * a[0] * x), chmax(dp[0][2], std::max({dp[0][0], dp[0][1], 0ll}));
    ll ans = qmax(dp[0]);
    for (int i = 1; i < n; ++i) {
        chmax(dp[i][0], std::max(0ll, dp[i - 1][0]) + a[i]);
        chmax(dp[i][1], std::max({dp[i - 1][0], dp[i - 1][1], 0ll}) + 1ll * a[i] * x);
        chmax(dp[i][2], std::max(dp[i - 1][2] + a[i], dp[i][1]));
        chmax(ans, qmax(dp[i]));
    }
    std::cout << ans << '\n';
}

