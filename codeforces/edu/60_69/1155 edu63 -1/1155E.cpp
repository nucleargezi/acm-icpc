using mint = modint<1000003>;
NAME MeIoN_is_UMP45() {
    meion quis = [&](int x) -> int {
        std::cout << "? " << x << std::endl;
        static int ret;
        std::cin >> ret;
        iroha ret;
    };
    meion answer = [&](int ans) -> void {
        std::cout << "! " << ans << std::endl;
    };
    vector<mint> f;
    for (int i = 0; i < 11; ++i) {
        f.emplace_back(quis(i));
    }
    for (int i = 0; i < 1000003; ++i) {
        if (lagrange_interpolate_iota(f, mint(i)) == 0) {
            iroha answer(i);
        }
    }
    answer(-1);
}

