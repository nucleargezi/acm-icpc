NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<int> a(n);
    std::cin >> a;
    priority_queue<int, vector<int>, greater<>> q;
    ll ans{};
    for (int i{n}; ~a[--i]; ) {
        q.emplace(a[i]);
        if (i + 1 & i) continue;
        ans += q.top();
        q.pop();
    }
    std::cout << ans << '\n';
}

