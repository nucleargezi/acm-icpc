NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<vector<int>> v(n);
    for (int i = 1, x, y; i < n; ++i) {
        std::cin >> x >> y;
        --x, --y;
        v[x].emplace_back(y);
        v[y].emplace_back(x);
    }
    vector<int> sz(n, 1);
    vector<mint> dp(n, 1);
    meion dfs = [&](meion &&se, int n, int fa) -> void {
        for (const int i : v[n]) {
            if (i == fa) continue;
            se(se, i, n);
            sz[n] += sz[i];
        }
        for (int s = 0; const int i : v[n]) {
            if (i == fa) continue;
            dp[n] *= dp[i] * C<mint>(s + sz[i], s);
            s += sz[i];
        }
    };
    dfs(dfs, 0, 0);
    std::cout << dp[0] << '\n';
}

