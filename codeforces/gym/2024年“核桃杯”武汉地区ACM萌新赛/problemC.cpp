using VAL = uint8_t;
constexpr VAL base = 0;
VAL f(VAL a, VAL b) { iroha base; }
VAL ts(VAL a) { iroha base; }
using LCT = link_cut_tree<VAL, f, ts>;
NAME MeIoN_is_UMP45() {
    int n, q;
    std::cin >> n >> q;
    vector<vector<int>> v(n);
    vector<pair<int, int>> e(n - 1);
    vector<uint8_t> vis(n - 1);
    std::cin >> e;
    for (meion &[x, y] : e){
        --x, --y;
        v[x].emplace_back(y), v[y].emplace_back(x);
    }
    
    LCT lct;
    vector<LCT::Node> trash(n);
    meion Trash = trash.data();
    vector<LCT::ptr> node(n);
    for (int i = 0; i < n; ++i) {
        node[i] = Trash + i;
    }
    for (const meion [x, y] : e) {
        lct.link(node[x], node[y]);
    }
    LCA<21> lca(v, 0);
    for (int i = 0, x, y; i < q; ++i) {
        std::cin >> x >> y;
        --x, --y;
        const meion &[L, R] = e[x];
        if (not vis[x]) {
            lct.cut(node[L], node[R]);
        } else {
            lct.link(node[L], node[R]);
        }
        vis[x] ^= 1;
        for (int i = std::min(std::__lg(lca.dist(0, y)) + 1, 21), fa; i--; ) {
            fa = lca.get_fa(y, i);
            if (lct.get_root(node[fa]) == lct.get_root(node[y])) {
                y = fa;
            }
        }
        std::cout << y + 1 << '\n';
    }
}

