NAME MeIoN_is_UMP45() {
    int n, m;
    std::cin >> n >> m;
    vector<int> c(m + 1);
    for (int i = 0, sz; i < n; ++i) {
        std::cin >> sz;
        for (int k = sz, l, r; k--; ) {
            std::cin >> l >> r, --l;
            ++c[l], --c[r];
        }
    }
    c = pre_sum(c);
    pair ans{inf<int>, -1};
    for (int i = 0; i < m; ++i) {
        chmin(ans, pair(n - c[i + 1], i + 1));
    }
    std::cout << ans.second << ' ' << ans.first << '\n';
}

