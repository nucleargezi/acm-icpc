NAME MeIoN_is_UMP45() {
    int n, m, k;
    std::cin >> n >> m >> k;
    vector<int> a(n), b(m), c(k);
    std::cin >> a >> b >> c;
    sort(a, greater());
    sort(b, greater());
    sort(c, greater());
    b.resize(n), c.resize(n);
    vector<pair<int, int>> so;
    for (int i = 0; i < n; ++i) {
        so.emplace_back(a[i], b[i]);
        if (c[i]) so.emplace_back(std::min(a[i], c[i]), 0);
    }
    sort(so);
    ll ans = 0, t = 0;
    for (int all = so.size(), pr = 0, d; meion [x, y] : so) {
        d = x - pr;
        t += 1ll * all-- * d;
        ans += std::min<ll>(t, y);
        pr = x;
    }
    std::cout << ans << '\n';
}

