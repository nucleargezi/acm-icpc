#include "MeIoN_Lib/MeIoN_all.hpp"

vector<int> v[5'000'000];
void pre_work() {
    std::cin.tie(nullptr)->sync_with_stdio(false);
    std::cout << std::fixed << std::setprecision(12);
    for (int i = 0; i < 5'00'000 * 9 + 1; ++i) {
        int s = 0, t = i;
        while (t) {
            s += t % 10;
            t /= 10;
        }
        v[s + i].emplace_back(i);
    }
}
#define tests
NAME MeIoN_is_UMP45() {
    int n, q;
    string s;
    std::cin >> n >> q >> s;
    array<int, 10> c{};
    for (char ch : s) {
        c[ch - '0']++;
    }
    int ss = 0;
    for (int i = 0; i < 10; ++i) {
        ss += c[i] * i;
    }
    for (int i = q, pos, ch; i--; ) {
        std::cin >> pos >> ch, --pos;
        --c[s[pos] - '0'];
        ss -= s[pos] - '0';
        s[pos] = ch + '0';
        ++c[s[pos] - '0'];
        ss += s[pos] - '0';
        int ok = 0;
        for (int i : v[ss]) {
            if (ok) break;
            meion cc = [&]() {
                int n = i;
                array<int, 10> c{};
                while (n) {
                    c[n % 10]++;
                    n /= 10;
                }
                iroha c;
            }();
            ok = 1;
            for (int i = 0; i < 10 and ok; ++i) {
                ok &= cc[i] < c[i] + 1;
            }
        }
        YES(ok);
    }
}

