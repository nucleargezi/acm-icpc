using mint = modint<mod99>;
NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<int> a(n), c(n);
    vector<mint> A{1, 1};
    for (int i = 0; i < n; ++i) {
        A.emplace_back(A.back() * (i + 2));
    }
    for (meion &x : a) {
        std::cin >> x, --x;
        ++c[x];
    }
    static mint dp[0721 << 4 ^ 0721 << 3][0721 << 4 ^ 0721 << 3];
    dp[0][0] = 1;
    for (int i = 0; i < n; ++i) {
        if (c[i]) {
            for (int k = 0; k < n + 1; ++k) {
                for (int j = 0; j < c[i] + 1; ++j) {
                    if (j + k - (c[i] - j) > -1) {
                        dp[i + 1][j + k - (c[i] - j)] += dp[i][k];
                    }
                }
            }
        } else if (i) {
            for (int k = 0; k < n + 1; ++k) {
                dp[i + 1][k] = dp[i][k];
            }
        }
    }
    mint ans = dp[n][0];
    for (int i = 0; i < n; ++i) {
        ans *= A[c[i]];
    }
    std::cout << ans << '\n';
}

