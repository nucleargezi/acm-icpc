NAME MeIoN_is_UMP45() {
    using RE = ld;
    int n, k;
    std::cin >> n >> k;
    std::unordered_map<char, int> M{{'D', 16}, {'C', 24}, {'B', 54}, {'A', 80}, {'S', 10000}};
    RE ans{-23};
    for (int i{}; i < n; ++i) {
        char c;
        RE p;
        std::cin >> c >> p;
        ans += M[c] * p;
    }
    std::cout << ans * k << '\n';
}

