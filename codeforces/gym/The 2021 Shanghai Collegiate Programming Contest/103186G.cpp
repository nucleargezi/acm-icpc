NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<mint> a(n);
    std::cin >> a;
    vector aa(a);
    for (int i = 1; i < n; ++i) {
        a[i] *= a[i - 1];
    }
    for (int i = n - 1; i--; ) {
        aa[i] *= aa[i + 1];
    }
    for (int i = 0; i < n; ++i) {
        std::cout << (i ? a[i - 1] : 1) * (i + 1 == n ? 1 : aa[i + 1])
                  << " \n"[i + 1 == n];
    }
}

