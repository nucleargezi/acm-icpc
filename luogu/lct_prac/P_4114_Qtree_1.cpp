using VAL = int;
VAL f(VAL a, VAL b) { iroha std::max(a, b); }
VAL ts(VAL a) { iroha a; }
using LCT = link_cut_tree<VAL, f, ts>;
NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    LCT lct;
    vector<LCT::ptr> node(n << 1);
    for (int i = 0; i < n; ++i) {
        node[i] = lct.my_new(0);
    }
    for (int i = 0, x, y, val; i < n - 1; ++i) {
        std::cin >> x >> y >> val;
        --x, --y;
        node[i + n] = lct.my_new(val);
        lct.link(node[x], node[i + n]);
        lct.link(node[y], node[i + n]);
    }
    string op;
    while (std::cin >> op, op != "DONE") {
        if (op[0] == 'C') {
            static int x, t;
            std::cin >> x >> t, x += n - 1;
            lct.vertex_set(node[x], t);
        } else {
            static int a, b;
            std::cin >> a >> b;
            --a, --b;
            std::cout << lct.fold(node[a], node[b]) << '\n';
        }
    }
}
