NAME MeIoN_is_UMP45() {
    int n, m;
    std::cin >> n >> m;
    vector<vector<int>> v(n);
    for (int i = 1, x, y; i < n; ++i) {
        std::cin >> x >> y, --x, --y;
        v[x].emplace_back(y);
        v[y].emplace_back(x);
    }
    vector<int> w(n);
    std::cin >> w;
    LCA<19> lca(v, 0);
    vector<int> in(n), dis(n), ans(n);
    vector<vector<int>> ends(n), anc(n);
    vector<pair<int, int>> go(m);
    for (int i = 0, x, y; i < m; ++i) {
        std::cin >> x >> y;
        --x, --y;
        int LCA = lca.lca(x, y);
        ++in[x];
        dis[i] = lca.dist(x, y);
        anc[LCA].emplace_back(i);
        ends[y].emplace_back(i);
        go[i] = {x, y};
        if (lca.dist(LCA, x) == w[LCA]) --ans[LCA];
    }
    vector<int> a(n << 1), b(a);
    const int N = n;
    meion dfs = [&](meion &&dfs, int n, int fa) -> void {
        int dis1 = a[lca.dist(n) + w[n]];
        int dis2 = b[w[n] - lca.dist(n) + N];
        for (int i : v[n]) {
            if (i == fa) continue;
            dfs(dfs, i, n);
        }
        a[lca.dist(n)] += in[n];
        for (int i : ends[n]) {
            ++b[dis[i] - lca.dist(go[i].second) + N];
        }
        ans[n] +=
            a[lca.dist(n) + w[n]] - dis1 + b[w[n] - lca.dist(n) + N] - dis2;
        for (int i : anc[n]) {
            --a[lca.dist(go[i].first)];
            --b[dis[i] - lca.dist(go[i].second) + N];
        }
    };
    dfs(dfs, 0, 0);
    std::cout << ans << '\n';
}

