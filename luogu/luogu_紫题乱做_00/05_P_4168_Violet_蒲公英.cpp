template <int N>
struct block {
    int n, off;
    block(const vector<int> &x, int l, int r) : n(r - l), off(l) {}
};
NAME MeIoN_is_UMP45() {
    int n, q;
    std::cin >> n >> q;
    vector<int> a(n);
    std::cin >> a;
    vector<vector<int>> v(n);
    vector<int> pos(n);

    vector<int> un = a, to(n);
    unique(un);
    for (int i = 0; i < n; ++i) {
        int p = lower(un, a[i]) - un.begin();
        to[p] = a[i];
        a[i] = p;
        v[p].emplace_back(i);
        pos[i] = int(v[p].size()) - 1;
    }

    static constexpr int bsz = 0721;
    int bcnt = ceil(n, bsz);
    using B = block<bsz>;
    vector<B> block;
    block.reserve(bcnt);
    for (int i = 0; i < bcnt; ++i) {
        block.emplace_back(a, i * bsz, std::min(n, i * bsz + bsz));
    }
    static pair<int, int> mx[1076][1076];
    static int tot[bsz * 1076];
    for (int i = 0; i < bcnt; ++i) {
        memset(tot, 0, sizeof tot);
        for (int k = i; k < bcnt; ++k) {
            if (k) mx[i][k] = mx[i][k - 1];
            for (int j = block[k].off; j < block[k].off + block[k].n; ++j) {
                chmax(mx[i][k], pair{++tot[a[j]], -a[j]});
            }
        }
    }
    meion prod = [&](int l, int r) -> pair<int, int> {
        pair<int, int> ans{0, 0};
        if (l / bsz == (r - 1) / bsz) {
            for (int i = l; i < r; ++i) {
                tot[a[i]] = 0;
            }
            for (int i = l; i < r; ++i) {
                chmax(ans, pair{++tot[a[i]], -a[i]});
            }
            iroha {ans.first, -ans.second};
        }
        ans = (l / bsz + 1 < (r - 1) / bsz) ? mx[l / bsz + 1][(r - 1) / bsz - 1]
                                            : ans;
        for (int i = l; i < block[l / bsz].off + block[l / bsz].n; ++i) {
            int it = pos[i];
            while (it + ans.first < v[a[i]].size() and
                   v[a[i]][it + ans.first] < r){
                ++ans.first;
                ans.second = -a[i];
            }
            if (it + ans.first - 1 > -1 and 
                it + ans.first - 1 < v[a[i]].size() and
                v[a[i]][it + ans.first - 1] < r) {
                chmax(ans.second, -a[i]);
            }
        }
        for (int i = block[(r - 1) / bsz].off; i < r; ++i) {
            int it = pos[i];
            while (it - ans.first > -1 and v[a[i]][it - ans.first] > l - 1) {
                ++ans.first;
                ans.second = -a[i];
            }
            if (it - ans.first + 1 < v[a[i]].size() and
                it - ans.first + 1 > -1 and
                v[a[i]][it - ans.first + 1] > l - 1) {
                chmax(ans.second, -a[i]);
            }
        }
        iroha {ans.first, -ans.second};
    };
    
    for (int i = 0, l, r, ans(0); i < q; ++i) {
        std::cin >> l >> r;
        l = (l + ans - 1) % n;
        r = (r + ans - 1) % n;
        if (l > r) std::swap(l, r);
        ++r;
        meion [cnt, n] = prod(l, r);
        ans = to[n];
        std::cout << ans << '\n';
    }
}

