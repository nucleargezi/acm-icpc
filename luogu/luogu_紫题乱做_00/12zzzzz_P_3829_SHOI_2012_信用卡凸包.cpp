using RE = ld;
NAME MeIoN_is_UMP45() {
    int n;
    RE a, b, r;
    std::cin >> n >> a >> b >> r;
    a /= 2, b /= 2;
    vector<point<RE>> points;
    for (int i = 0; i < n; ++i) {
        static RE x, y, theta;
        std::cin >> x >> y >> theta;
        points.emplace_back( - (b - r), + (a - r));
        points.back() = points.back().rotate(theta);
        points.back() += {x, y};
        points.emplace_back( + (b - r), + (a - r));
        points.back() = points.back().rotate(theta);
        points.back() += {x, y};
        points.emplace_back( + (b - r), - (a - r));
        points.back() = points.back().rotate(theta);
        points.back() += {x, y};
        points.emplace_back( - (b - r), - (a - r));
        points.back() = points.back().rotate(theta);
        points.back() += {x, y};
    }
    vector rk = convex_hull(points);
    RE ans = 2 * r * pi;
    for (int i = 0; i < rk.size(); ++i) {
        ans += dist<RE>(points[rk[i]], points[rk[(i + 1) % rk.size()]]);
    }
    std::cout << ans << '\n';
}

