// #define tests
NAME MeIoN_is_UMP45() {
    int n;
    string s;
    std::cin >> n >> s;
    SAM sam(n << 1);
    for (int pla = 0; const char c : s) {
        pla = sam.ext(pla, c - 'a');
    }
    ll ans = 0;
    // 每个pos存着所有结束pos相同的子串集合, len为最长字串, link_pos的最长字串length为
    // pos的 最短字串 的length - 1
    for (int i = 1; i < sam.size(); ++i) {
        // 最长len - 最短len + 1 为串数量
        ans += sam[i].len - sam[sam[i].link].len;
    }
    std::cout << ans << '\n';
}

