NAME MeIoN_is_UMP45() {
    string s, ss;
    std::cin >> s;
    const int n = s.length();
    array<SAM, 11> sam;
    int tot = 0;
    while (std::cin >> ss) {
        for (int pla = 0; const char c : ss) {
            pla = sam[tot].ext(pla, c - 'a');
        }
        ++tot;
    }
    vector<int> slen(n, inf<int>);
    for (int i = 0; i < tot; ++i) {
        int pla = 0, plen = 0;
        meion &sa = sam[i];
        for (int i = 0; const char c : s) { 
            if (sa[pla][c - 'a'] == -1) {
                while (pla != 0 and sa[pla][c - 'a'] == -1) {
                    pla = sa[pla].link;
                }
                plen = sa[pla].len;
            }
            if (sa[pla][c - 'a'] != -1) {
                pla = sa[pla][c - 'a'];
                ++plen;
            }
            chmin(slen[i++], plen);
        }
    }
    std::cout << qmax(slen) << '\n';
}

