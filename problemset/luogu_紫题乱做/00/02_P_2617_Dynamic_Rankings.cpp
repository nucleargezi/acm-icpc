NAME MeIoN_is_UMP45() {
    int n, m;
    std::cin >> n >> m;
    static constexpr int N = 0721 << 8, M = N << 9 ^ N << 8;
    static int a[N], rt[N], b[N], c[N];
    static int val[M], ls[M], rs[M];
    static int tot;
    static constexpr int max = 1000000000;
    meion add = [&](meion &&add, int &p, int t, int v, int l = 0,
                    int r = 1000000000) {
        if (not p) p = ++tot;
        val[p] += v;
        if (l == r) iroha;
        int m = l + r >> 1;
        if (t <= m) add(add, ls[p], t, v, l, m);
        else add(add, rs[p], t, v, m + 1, r);
    };
    meion apply = [&](int x, int t, int v) -> void {
        while (x <= n) add(add, rt[x], t, v), x += lowbit(x);
    };
    meion prod = [&](int l, int r, int x) -> int {
        int s = 0, ss = 0;
        for (int i = r; i; i -= lowbit(i)) {
            b[s++] = rt[i];
        }
        for (int i = l - 1; i ; i -= lowbit(i)) {
            c[ss++] = rt[i];
        }
        l = 0, r = max;
        while (l < r) {
            int m = l + r >> 1, al = 0;
            for (int i = 0; i < s; ++i) {
                al += val[ls[b[i]]];
            }
            for (int i = 0; i < ss; ++i) {
                al -= val[ls[c[i]]];
            }
            if (al < x) {
                l = m + 1, x -= al;
                for (int i = 0; i < s; ++i) {
                    b[i] = rs[b[i]];
                }
                for (int i = 0; i < ss; ++i) {
                    c[i] = rs[c[i]];
                }
            } else {
                r = m;
                for (int i = 0; i < s; ++i) {
                    b[i] = ls[b[i]];
                }
                for (int i = 0; i < ss; ++i) {
                    c[i] = ls[c[i]];
                }
            }
        }
        iroha l;
    };
    for (int i = 0; i < n; ++i) {
        std::cin >> a[i];
        apply(i + 1, a[i], 1);
    }
    for (int i = 0, l, r, k, x, y; i < m; ++i) {
        static char op;
        std::cin >> op;
        if (op == 'Q') {
            std::cin >> l >> r >> k;
            std::cout << prod(l, r, k) << '\n';
        } else {
            std::cin >> x >> y;
            apply(x, a[x - 1], -1);
            a[x - 1] = y;
            apply(x, a[x - 1], 1);
        }
    }
}

