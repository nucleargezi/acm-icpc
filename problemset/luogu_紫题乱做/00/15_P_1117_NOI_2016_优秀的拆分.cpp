NAME MeIoN_is_UMP45() {
    string s;
    std::cin >> s;
    HASH h(s);
    const int n = s.length();
    vector<int> a(n + 1), b(a);
    for (int len = 1; len < n + 1; ++len) {
        int i = len - 1, k = (len << 1) - 1;
        while (k < n) {
            int lcp = get_lcs(h, i + 1 - len, i + 1, h, k + 1 - len, k + 1);
            int lcs = get_lcp(h, i, n, h, k, n);
            int e = std::max(k - lcp + len, k);
            int ee = std::min(k + lcs - 1, k + len - 1);
            if (e < ee + 1) {
                ++a[e], --a[ee + 1];
                ++b[e - len * 2 + 1], --b[ee - len * 2 + 2];
            }
            i += len, k += len;
        }
    }
    a = pre_sum(a), b = pre_sum(b);
    ll ans = 0;
    for (int i = 1; i < n; ++i) {
        ans += 1ll * a[i] * b[i + 1];
    }
    std::cout << ans << '\n';
}

