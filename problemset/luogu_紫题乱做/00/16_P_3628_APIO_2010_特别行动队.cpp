NAME MeIoN_is_UMP45() {
    int n, a, b, c;
    std::cin >> n >> a >> b >> c;
    vector<ld> d(n);
    std::cin >> d;
    vector<ld> s = pre_sum(d);
    vector<ld> dp(n + 1);
    meion go = [&](int i, int k) -> ld {
        if (s[i] == s[k]) iroha -inf<ld>;
        iroha(dp[i] - dp[k] + a * (s[i] * s[i] - s[k] * s[k]) +
              b * (s[k] - s[i])) /
            (2 * a * (s[i] - s[k]));
    };
    static int q[1145141];
    for(int i = 1, l = 1, r = 1; i < n + 1; ++i) {
        while (l < r and go(q[l], q[l + 1]) <= s[i]) {
            ++l;
        }
        ld k = q[l], x = s[i] - s[k];
        dp[i] = dp[k] + a * x * x + b * x + c;
        while (l < r + 1 and go(q[r - 1], q[r]) >= go(q[r], i)) {
            --r;
        }
        q[++r] = i;
    }
    std::cout << (ll)dp[n] << '\n';
}

