using Node = LCT_Node_Base;
using LCT = Link_Cut_Tree<Node>;
NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    LCT lct(n + 1);
    vector<int> a(n);
    for (int i = 0; i < n; ++i) {
        std::cin >> a[i];
        lct.link(i, std::min(n, i + a[i]));
    }
    
    int m;
    std::cin >> m;
    for (int i = 0, op, x, k; i < m; ++i) {
        std::cin >> op;
        if (op == 1) {
            std::cin >> x;
            lct.evert(x);
            lct.expose(n);
            std::cout << lct[n]->size - 1 << '\n';
        } else {
            std::cin >> x >> k;
            lct.cut(x, std::min(n, x + a[x]));
            a[x] = k;
            lct.link(x, std::min(n, x + a[x]));
        }
    }
}

