#include "MeIoN_Lib/MeIoN_all.hpp"

constexpr int N = 1'0000'000 + 0721;
int v[N], prim[N], f[N], s[N];
ll mu[N];

void before() {
    std::cin.tie(nullptr)->sync_with_stdio(false);
    std::cout << std::fixed << std::setprecision(12);

    mu[1] = 1;
    int tt = 1;
    for (int i = 2; i < N; ++i) {
        if (not v[i]) {
            prim[tt++] = i, mu[i] = -1;
        }
        for (int k = 1; k < tt and i * prim[k] < N; ++k) {
            v[i * prim[k]] = 1;
            if (i % prim[k] == 0) break;
            mu[i * prim[k]] = -mu[i];
        }
    }
    for (int i = 1; i < tt; ++i) {
        for (int k = 1; prim[i] * k < N; ++k) {
            f[k * prim[i]] += mu[k];
        }
    }
    for (int i = 1; i < N; ++i) {
        s[i] = s[i - 1] + f[i];
    }
}
#define tests
NAME MeIoN_is_UMP45() {
    int n, m;
    std::cin >> n >> m;
    if (n > m) std::swap(n, m);
    ll ans = 0;
    for (int l = 1, r = 0; l < n + 1; l = r + 1) {
        r = std::min(n / (n / l), m / (m / l));
        ans += 1ll * (s[r] - s[l - 1]) * (n / l) * (m / l);
    }
    std::cout << ans << '\n';
}

