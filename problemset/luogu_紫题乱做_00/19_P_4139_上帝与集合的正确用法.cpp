#include "MeIoN_Lib/MeIoN_all.hpp"

vector<int> phi, p;
vector<uint8_t> vis;
void sieve(int n) {
    phi.resize(n + 1);
    p.resize(n + 1);
    vis.resize(n + 1);
    int tot = 0;
    phi[1] = 1;
    for (int i = 2; i < n + 1; ++i) {
        if (not vis[i]) {
            p[++tot] = i;
            phi[i] = i - 1;
        }
        for (int k = 1; k < tot + 1 and i * p[k] < n + 1; ++k) {
            vis[i * p[k]] = 1;
            if (i % p[k] == 0) {
                phi[i * p[k]] = phi[i] * p[k];
                break;
            } else {
                phi[i * p[k]] = phi[i] * phi[p[k]];
            }
        }
    }
}

void before() {
    sieve(1'000'000'0);
    std::cin.tie(nullptr)->sync_with_stdio(false);
    std::cout << std::fixed << std::setprecision(12);
}
