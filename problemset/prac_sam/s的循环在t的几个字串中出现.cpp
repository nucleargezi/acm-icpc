NAME MeIoN_is_UMP45() {
    string s;
    std::cin >> s;
    SAM sam;
    meion [sz, v] = sam.build(s);
    static bitset<2000001> vis;
    int q;
    std::cin >> q;
    for (int i = 0; i < q; ++i) {
        std::cin >> s;
        vis.reset();
        const int n = s.length();
        int slen = 0, pla = 0;
        for (const char c : s) {
            while (pla != 0 and sam[pla][c - 'a'] == -1) {
                pla = sam[pla].link;
                slen = sam[pla].len;
            }
            if (sam[pla][c - 'a'] != -1) {
                pla = sam[pla][c - 'a'];
                ++slen;
            }
        }
        ll ans = 0;
        for (int i = 0; i < n; ++i) {
            if (slen == n) {
                if (not vis[pla]) {
                    ans += sz[pla];
                }
                vis[pla] = 1;
                --slen;
                if (slen == sam[sam[pla].link].len) {
                    pla = sam[pla].link;
                }
            }
            while (pla != 0 and sam[pla][s[i] - 'a'] == -1) {
                pla = sam[pla].link;
                slen = sam[pla].len;
            }
            if (sam[pla][s[i] - 'a'] != -1) {
                pla = sam[pla][s[i] - 'a'];
                ++slen;
            }
        }
        std::cout << ans << '\n';
    }
}

