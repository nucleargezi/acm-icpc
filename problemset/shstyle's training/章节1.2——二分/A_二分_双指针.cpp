NAME MeIoN_is_UMP45() {
    int n, c;
    std::cin >> n >> c;
    vector<int> a(n);
    std::cin >> a;
    map<int, int> m;
    ll ans{};
    for (int x : a) {
        ans += m[x + c];
        ans += m[x - c];
        ++m[x];
    }
    std::cout << ans << '\n';
}

