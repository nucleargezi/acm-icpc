using mint = dmint;
NAME MeIoN_is_UMP45() {
    int n, q, mod;
    std::cin >> n >> q >> mod;
    mint::set_mod(mod);
    vector<mint> fib{0, 1};
    for (int i{}; i < n + 12; ++i) {
        fib.emplace_back(fib.end()[-1] + fib.end()[-2]);
    }
    vector<mint> a(n);
    for (int i{}; i < n + n; ++i) {
        ll x;
        std::cin >> x;
        a[i % n] += (i < n ? x : -x);
    }
    vector<mint> c(n + 2);
    for (int i{}; i < n; ++i) {
        c[i] += a[i], c[i + 1] -= a[i], c[i + 2] -= a[i];
    }
    ll s{};
    for (meion x : c) {
        s += x.val;
    }
    meion go = [&](int i, ll x) {
        s -= c[i].val;
        c[i] += x;
        s += c[i].val;
    };
    for (int i{}, l, r; i < q; ++i) {
        char c;
        std::cin >> c >> l >> r, --l;
        ll f = (c == 'A' ? 1 : -1);
        ll sz = r - l;
        go(l, f);
        go(r, -f * fib[sz + 1].val);
        go(r + 1, -f * fib[sz].val);
        NO(s);
    }
}

