NAME MeIoN_is_UMP45() {
    int n, m;
    std::cin >> n >> m;
    int a[n][n + 2];
    memset(a, 0, sizeof a);
    for (int i{}, x, xx, y, yy; i < m; ++i) {
        std::cin >> x >> y >> xx >> yy, --x;
        for (int i = x; i < xx; ++i) {
            ++a[i][y];
            --a[i][yy + 1];
        }
    }
    for (int i{}; i < n; ++i) {
        for (int k{1}; k < n + 1; ++k) {
            a[i][k] += a[i][k - 1];
            std::cout << a[i][k] << " \n"[k == n];
        }
    }
}

