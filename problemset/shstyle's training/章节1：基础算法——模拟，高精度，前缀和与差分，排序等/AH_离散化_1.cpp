NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<int> lr(n << 1);
    std::cin >> lr;
    vector u(lr);
    for (int i{}; i < n; ++i) {
        u.emplace_back(u[i << 1 | 1] - 1);
    }
    unique(u);
    vector<int> c(u.size() + 1);
    for (int i{}, l, r; i < (n << 1); i += 2) {
        l = lower(u, lr[i]) - u.begin(), r = lower(u, lr[i + 1]) - u.begin();
        ++c[l], --c[r];
    }
    for (int i{1}; i < c.size(); ++i) {
        c[i] += c[i - 1];
    }
    int ans{};
    for (int i = 0; i < c.size() - 1; ++i) {
        if (c[i]) {
            if (i < c.size() - 1 and c[i + 1]) {
                ans += u[i + 1] - u[i] - 1;
            }
            ++ans;
        }
    }
    std::cout << ans << '\n';
}

