NAME MeIoN_is_UMP45() {
    int n, m;
    std::cin >> n >> m;
    int a[n][n], b[n][n];
    for (int i{}; i < n * n; ++i) {
        a[0][i] = i + 1;
    }
    for (int i{}, x, y, r, z; i < m; ++i) {
        std::cin >> x >> y >> r >> z, --x, --y;
        int px = x - r, py = y - r, sz = r + r + 1;
        for (int i{}; i < sz; ++i) {
            for (int k{}; k < sz; ++k) {
                (z ? b[sz - k - 1][i] : b[k][sz - i - 1]) = a[px + i][py + k];
            }
        }
        for (int i{}; i < sz; ++i) {
            for (int k{}; k < sz; ++k) {
                a[px + i][py + k] = b[i][k];
            }
        }
    }
    for (int i{}; i < n; ++i) {
        for (int k{}; k < n; ++k) {
            std::cout << a[i][k] << " \n"[k + 1 == n];
        }
    }
}

