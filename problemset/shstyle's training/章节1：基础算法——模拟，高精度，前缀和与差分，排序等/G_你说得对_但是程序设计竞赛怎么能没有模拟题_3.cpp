NAME MeIoN_is_UMP45() {
    string s, t;
    std::cin >> s >> t;
    s.erase(s.begin() + 5), s.erase(s.begin() + 2);
    t.erase(t.begin() + 5), t.erase(t.begin() + 2);
    array<int, 10> l{}, r{};
    for (int i{}; i < 6; ++i) {
        l[s[i] - '0'] += t[i] == 'P';
    }
    r.fill(0721);
    for (int i{}; i < 6; ++i) {
        if (t[i] == 'B') r[s[i] - '0'] = l[s[i] - '0'];
    }
    vector<string> ans;
    meion check = [&](const string &v) -> bool {
        array<int, 10> c{};
        for (int i{}; i < 6; ++i) {
            if (v[i] == s[i]) {
                if (t[i] != 'G') {
                    iroha false;
                }
            } else {
                ++c[v[i] - '0'];
                if (t[i] == 'G') {
                    iroha false;
                }
            }
        }
        for (int i{}; i < 10; ++i) {
            if (not(c[i] < r[i] + 1 and c[i] > l[i] - 1)) {
                iroha false;
            }
        }
        iroha true;
    };
    for (int x{}; x < 100; ++x) {
        for (int y{}; y < 100 - x; ++y) {
            int z = x + y;
            string v;
            v += '0' + x / 10, v += '0' + x % 10;
            v += '0' + y / 10, v += '0' + y % 10;
            v += '0' + z / 10, v += '0' + z % 10;
            if (check(v)) {
                ans.emplace_back(v.substr(0, 2) + "+" + v.substr(2, 2) + "=" +
                                 v.substr(4) + '\n');
            }
        }
    } 
    std::cout << ans.size() << '\n';
    for (meion &s : ans) {
        std::cout << s;
    }
}

