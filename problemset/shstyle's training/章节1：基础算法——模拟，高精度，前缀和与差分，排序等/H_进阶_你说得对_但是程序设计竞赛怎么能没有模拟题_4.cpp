NAME MeIoN_is_UMP45() {
    int n;
    string o;
    std::cin >> n >> o;
    int kd{}, sn{};
    for (char c : o) {
        if (c == 'n') kd = 1;
        if (std::isdigit(c)) {
            sn = sn * 10 + c - '0';
        }
    }
    if (not kd) sn = 0;
    vector<pair<string, int>> stk;
    set<string> se;
    bool ok = true;
    int max{};
    vector<int> a(n);
    for (int i{}, val{}, C{}; i < n; ++i) {
        char c; 
        std::cin >> c;
        if (c == 'F') {
            string it, sx, sy;
            std::cin >> it >> sx >> sy;
            int x{}, y{};
            for (char c : sx) {
                x = x * 10 + c - '0';
            }
            for (char c : sy) {
                y = y * 10 + c - '0';
            }
            if (not se.emplace(it).second) {
                ok = false;
            }
            if ((sx == "n" and sy != "n") or
                (sx != "n" and sy != "n" and x > y)) {
                a[i] = -1;
            } else if (sx == "n") {
                ;
            } else if (sy == "n") {
                a[i] = 1;
            } else {
                ;
            }
            if (a[i] == 1) {
                ++val;
            } else if (a[i] == -1) {
                ++C;
            }
            stk.emplace_back(it, i);
        } else {
            if (stk.empty()) {
                ok = false;
                continue;
            }
            if (not C) chmax(max, val);
            if (a[stk.back().second] == -1) {
                --C;
            } else if (a[stk.back().second] == 1) {
                --val;
            } 
            if (se.contains(stk.back().first)) {
                se.erase(stk.back().first);
            }
            stk.pop_back();
        }
    }
    if (not stk.empty() or not ok) {
        std::cout << "ERR\n";
    } else {
        Yes(max == sn);
    }
}

