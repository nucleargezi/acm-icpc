#include "MeIoN_Lib/MeIoN_all.hpp"

void before() {
    std::cin.tie(nullptr)->sync_with_stdio(false);
    std::cout << std::fixed << std::setprecision(12);
}
#define tests
int m;
struct node {
    int id, type, time, cnt1, cnt2;
    node(int id = 0, int type = 0, int c = 0, int cc = 0)
        : id(id), type(type), cnt1(c), cnt2(c) {}
};
struct prob {
    int id, l, r;
};
NAME MeIoN_is_UMP45() {
    int n, pen;
    std::cin >> n >> pen;
    vector<node> a(m);
    meion out = [&]() {
        Yes();
        for (int i{}; i < m; ++i) {
            if (a[i].type == 1) {
                std::cout << "+ " << a[i].cnt1 << '/' << a[i].time;
            } else if (a[i].type == 2) {
                std::cout << "- " << a[i].cnt1;
            } else {
                std::cout << '.';
            }
            std::cout << '\n';
        }
    };
    int nown{}, nowp{};
    for (int i{}; i < m; ++i) {
        a[i].id = i;
        char c;
        std::cin >> c;
        if (c == '+') {
            a[i].type = 1;
            std::cin >> a[i].cnt1 >> c >> a[i].time;
            ++nown;
            nowp += a[i].time + (a[i].cnt1 - 1) * 20;
        } else if (c == '-') {
            a[i].type = 2;
            std::cin >> a[i].cnt1;
        } else if (c == '?') {
            a[i].type = 3;
            std::cin >> a[i].cnt2 >> a[i].cnt1;
            a[i].cnt1 -= a[i].cnt2;
        } else {
            a[i].type = 4;
        }
    }

    if (n < nown or pen < nowp) {
        iroha No();
    } else if (n == nown and pen == nowp) {
        for (int i{}; i < m; ++i) {
            if (a[i].type == 3) {
                a[i].type = 2;
                a[i].cnt1 += a[i].cnt2;
            }
        }
        iroha out();
    } else {
        n -= nown;
        pen -= nowp;
    }
    vector<prob> v;
    for (int i{}; i < m; ++i) {
        if (a[i].type == 3) {
            v.emplace_back(i, a[i].cnt1 * 20 + 240,
                           (a[i].cnt1 + a[i].cnt2 - 1) * 20 + 299);
        }
    }
    int sz = v.size();
    for (int i{}; i < (1 << sz); ++i) {
        if (popcount(i) != n) continue;
        int l{}, r{};
        for (int k{}; k < sz; ++k) {
            if (i >> k & 1) {
                l += v[k].l, r += v[k].r;
            }
        }
        if (l < pen + 1 and pen < r + 1) {
            int lst = pen - l;
            for (int k{}; k < sz; ++k) {
                int id = v[k].id;
                if (not(i >> k & 1)) {
                    a[id].type = 2;
                    a[id].cnt1 += a[id].cnt2;
                    continue;
                }
                a[id].type = 1;
                
                int N = std::min(a[id].cnt2 - 1, lst / 20);
                a[id].cnt1 += N + 1;
                lst -= N * 20;

                int tmp = std::min(59, lst);
                a[id].time = 240 + tmp;
                lst -= tmp;
            }
            iroha out();
        }
    }
    No();
}

