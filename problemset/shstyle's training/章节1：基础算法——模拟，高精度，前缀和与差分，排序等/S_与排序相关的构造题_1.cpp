NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    vector<int> a(n);
    std::cin >> a;
    vector<int> id(n);
    for (int i = 0, x; i < n; ++i) {
        std::cin >> x;
        id[--x] = i;
    }
    for (int i = 0; i < n; ++i) {
        a[i] = id[--a[i]];
    }
    string ans(n * n, '1');
    for (int i = 0; i < n * n; ++i) {
        int x = i % n, y = x + 1;
        if (y == n) continue;
        if (a[x] > a[y]) {
            std::swap(a[x], a[y]);
            ans[i] = '2';
        }
    }
    std::cout << ans << '\n';
}

