NAME MeIoN_is_UMP45() {
    int n;
    std::cin >> n;
    ll a[n + 1][n + 1];
    memset(a, 0, sizeof a);
    for (int i{1}; i < n + 1; ++i) {
        for (int k{1}; k < n + 1; ++k) {
            std::cin >> a[i][k];
            a[i][k] += a[i][k - 1];
        }
        for (int k{1}; k < n + 1; ++k) {
            a[i][k] += a[i - 1][k];
        }
    }
    ll ans = -inf<ll>;
    for (int x{}; x < n; ++x) {
        for (int y{}; y < n; ++y) {
            for (int xx{x + 1}; xx < n + 1; ++xx) {
                for (int yy{y + 1}; yy < n + 1; ++yy) {
                    chmax(ans, a[xx][yy] + a[x][y] - a[xx][y] - a[x][yy]);
                }
            }
        }
    }
    std::cout << ans << '\n';
}

