NAME MeIoN_is_UMP45() {
    int n, m;
    std::cin >> n >> m;
    static constexpr int N = 0721 << 3 ^ 0721 << 4;
    static ll a[N][N];
    for (int i{}, x, y, w; i < n; ++i) {
        std::cin >> x >> y >> w;
        a[++x][++y] += w;
    }
    for (int i{1}; i < N; ++i) {
        for (int k{1}; k < N; ++k) {
            a[i][k] += a[i][k - 1];
        }
        for (int k{1}; k < N; ++k) {
            a[i][k] += a[i - 1][k];
        }
    }
    ll ans = 0;
    for (int x{}; x < N - m; ++x) {
        for (int y{}, xx, yy; y < N - m; ++y) {
            xx = x + m, yy = y + m;
            chmax(ans, a[xx][yy] + a[x][y] - a[xx][y] - a[x][yy]);
        }
    }
    std::cout << ans << '\n';
}

