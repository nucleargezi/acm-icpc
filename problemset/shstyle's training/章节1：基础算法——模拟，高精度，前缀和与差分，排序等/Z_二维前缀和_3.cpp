NAME MeIoN_is_UMP45() {
    int n, m, k, l;
    std::cin >> n >> m >> k >> l;
    int a[n + 2][m + 2];
    memset(a, 0, sizeof a);
    for (int i{1}; i < n + 1; ++i) {
        for (int k{1}; k < m + 1; ++k) {
            std::cin >> a[i][k]; 
            a[i][k] += a[i][k - 1];
        }
        for (int k{1}; k < m + 1; ++k) {
            a[i][k] += a[i - 1][k];
        }
    }
    ll ans{};
    for (int x = 1; x < k + 1; ++x) {
        for (int y = 1; y < l + 1; ++y) {
            ans += std::abs(a[n + x - k][m + y - l] + a[x - 1][y - 1] -
                            a[n + x - k][y - 1] - a[x - 1][m + y - l]);
        }
    }
    std::cout << ans << '\n';
}

